              <h2>About Prince Edward Island</h2>
							<p>Prince Edward Island is one of the three Maritime Provinces and is the smallest province in both land area and population.The island has several informal names: "Garden of the Gulf," referring to the pastoral scenery and lush agricultural lands throughout the province; and "Birthplace of Confederation" or "Cradle of Confederation", referring to the <a href="https://en.wikipedia.org/wiki/Charlottetown_Conference"  target=_blank>Charlottetown Conference</a> in 1864, although PEI did not join Confederation until 1873, when it became the seventh Canadian province. Prince Edward Island presents itself as the "Birthplace of Confederation" and this is commemorated through several buildings, a ferry vessel, and the <a href= "https://en.wikipedia.org/wiki/Confederation_Bridge" target=_blank>Confederation Bridge</a>.</p> 
							<p>Prince Edward Island is located in the Gulf of St. Lawrence, west of Cape Breton Island, north of the Nova Scotia peninsula, and east of New Brunswick. Its southern shore bounds the Northumberland Strait. </p>
							<p>The island has two urban areas. The largest surrounds Charlottetown Harbour, situated centrally on the island's southern shore, and consists of the capital city <a href= "https://en.wikipedia.org/wiki/Charlottetown" target=_blank>Charlottetown</a>, and suburban towns Cornwall and Stratford and a developing urban fringe.</p> 
							<p>The island's landscape is pastoral. Rolling hills, woods, reddish white sand beaches, ocean coves and the famous red soil have given Prince Edward Island a reputation as a province of outstanding natural beauty. The provincial government has enacted laws to preserve the landscape through regulation, although there is a lack of consistent enforcement, and an absence of province-wide zoning and land-use planning. Under the Planning Act of the province, municipalities have the option to assume responsibility for land-use planning through the development and adoption of official plans and land use bylaws. Thirty-one municipalities have taken responsibility for planning. In areas where municipalities have not assumed responsibility for planning, the Province remains responsible for development control.</p>
							
							<p>Lucy Maud Montgomery, who was born in Clifton (now New London) in 1874, wrote some 20 novels and numerous short stories that have been collected into anthologies. Her first Anne book <a href= "https://en.wikipedia.org/wiki/Anne_of_Green_Gables" target=_blank>Anne of Green Gables</a> was published in 1908. The musical play Anne of Green Gables has run every year at the Charlottetown festival for more than four decades. The sequel, Anne & Gilbert, premiered in the Playhouse in Victoria in 2005. The actual location of Green Gables, the house featured in Montgomery's Anne books, is in <a href= "https://en.wikipedia.org/wiki/Cavendish,_Prince_Edward_Island" target=_blank>Cavendish</a>, on the north shore of PEI.</p>
