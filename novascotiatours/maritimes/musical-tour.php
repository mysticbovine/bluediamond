						<p>Music plays a large role in our culture and heritage so we're proud to be able to share our foot tapping trip with you.  From Celtic Cape Breton to the country sounds of the South Shore, we hope this tour will educate you on our musical heritage and leave you singing a tune or two!  Let the musical memories begin!</p>

						<h2>Day One: Sydney, Cape Breton, Nova Scotia</h2>
						<p>Upon collection from your hotel in Halifax, we'll head to Cape Breton (approximately a 4hr drive), with a stop for lunch along the way.  We'll 'drop by' for afternoon tea at Rita MacNeils Tea Room, in the tiny community of Big Pond, Rita's birthplace.  Rita was an awarding winning singer/artist whose songs were about life, ordinary people and dreams that could be.  Perhaps one of her most famous songs and a huge international hit, was "Working Man".  This song was inspired by a trip she took down one of the local coal mines and is often sung by 'Men of the Deeps' - a chorus ensemble comprised solely of men who have worked in a coal mine.</p>

						<p>While the words of "Working Man" are still singing in your head, it would only be appropriate to visit the Miners Museum which is home to 'Men of the Deeps'.  We'll tour this mine, take a trip underground and learn first-hand, from miners themselves, what this harsh life was like.</p>

						<p>Our evening will be spent in Sydney where you can see the world's largest fiddle, perhaps stumble upon a ceilidh or enjoy an evening of live music at one of the local eateries, the choice is yours.</p>

						<h2>Day Two:  Glenville, Cape Breton, Nova Scotia</h2>
						<p>Our morning drive will take us along the lower portion of the world famous Cabot trail, through the beautiful Margaree Valley to Mabou, home of Cape Bretons Rankin family.  The Rankin Family is an award winning, Celtic, musical family comprising of 12 siblings!    They own the Red Shoe Pub in Mabou which is where we'll lunch and enjoy Celtic sounds.</p>

						<p>A short drive away is the Celtic Music Interpretive Centre where you're sure to have a musical afternoon to remember!  Here, not only learn about the deep roots of Celtic music in Nova Scotia, you can also enjoy a hands-on experience through workshops, one-on-one demonstrations and if you're feeling confident, perhaps record your own music!</p>

						<p>A brief drive along the coastline will take us to our evening accommodation - Canada's only single malt Inn and Distillery.  We'll take a short tour of the distillery, dine in the on-site restaurant (which was voted 'Best Restaurant on Cape Breton Island'), enjoy the sounds of local musicians and bask in the magnificent setting.  Beautiful food, music and scenery - does it get any better?</p>

						<h2>Day Three:  Charlottetown, Prince Edward Island</h2>
						<p>Leaving the lilt of Celtic sounds behind us, we'll head to Pictou, "the Birthplace of New Scotland".  If time allows, we can explore 'Ship Hector', the vessel Scottish settlers arrived on in 1773 (the beginning of our Celtic roots), prior to boarding the ferry to Prince Edward Island.</p>
						 
						<p>Spend some time exploring Charlottetown, the "birth place of Confederation" and enjoy a meal at one of the numerous, superb pubs or restaurants prior to an evening of entertainment at Confederation of the Arts.  Following the show, we'll take you to your hotel on the coastline, just a short drive away.</p>
						 
						<h2>Day Four: Summerside, Prince Edward Island</h2>
						<p>Prince Edward Island is known as the "Gentle Island", so adhering to that philosophy, we'll take a gentle drive across the island, perhaps stopping to learn more about Anne of Green Gables or visiting one of PEI's gorgeous beaches, before visiting the childhood home of one of Canada's most iconic, country song writers, "Stompin' Tom Connors".  "Stompin' Tom" is undoubtedly a Canadian musical legend and a name well known in hockey circles ('The Hockey Song' - popularly played in both Canadian and American hockey leagues).  His humorous songs and foot tapping tunes are sure to engage you!  We're on PEI, land of red mud and potatoes, time to listen to "Bud the Spud"!</p>
						 
						<p>We'll spend the evening in Summerside where you can visit the College of piping, stroll along the waterfront and perhaps take in a show at the Harbourfront Theatre.  The night is yours.</p>
						 
						<h2>Day Five:  Halifax, Nova Scotia</h2>
						<p>Leaving PEI, with a quick shopping stop in Borden, we'll travel over Confederation Bridge (the world's 'longest bridge over ice-covered water') and head towards Springhill, home of Anne Murray, yet another amazing voice from a small, coal mining town.  With countless awards and selling over 50-million albums, Anne Murray's voice is a heartwarming, international, household name.  Learn about this Canadian icon, her contribution to Canada's music industry and perhaps even create your own "duet" in her recording studio!</p>

						<p>From Springhill, we'll meander along the Bay of Fundy coastline, popping in at scenic spots along the way, until we reach the Glooscap Heritage Centre.  Here you can learn about our Mi'Kmaq heritage, enjoy an interactive tour and experience First Nation music and the important, cultural role it plays in our past and present.</p>

						<p>We'll spend the evening in Halifax where you can visit the Nova Scotia Music Hall of Fame, indulge yourself in fine dining and enjoy a variety of live music (Celtic and Blues to smooth jazz - it's all  here). Relax and enjoy the variety of Halifax nightlife.  Prior to returning to your downtown hotel, enjoy a peaceful, scenic, night-light stroll along the Halifax boardwalk with sounds of the Maritimes playing in your head accompanied by lapping waves and smells of the ocean.  Life is good!</p>

						<h2>Day Six: South Shore, Nova Scotia</h2>
						<p>Following breakfast in Halifax, we'll head along the South Shore coastline of Nova Scotia - named by National Geographic as one of the Top 10 Coastal Destinations in the world!  We'll visit the popular destination of Peggy's Cove and travel through the scenic coastal communities of Chester and Mahone Bay, then lunch in Lunenburg - home port of the famous Canadian Schooner, Bluenose II.</p>

						<p>We'll continue our scenic drive along the picturesque shoreline to Liverpool, where we'll visit the legendary Hank Snow Museum.  Hank Snow is a Canadian country artist who, over 40+ years, became an international, award winning singer, selling over 70 million records!  Explore the life and times of this wonderful Canadian artist, the "Singing Ranger".</p>

						<p>Our evening will be spent at one of Nova Scotia's popular beach resorts where you can relax, unwind and enjoy spectacular sunsets, or if you're craving more entertainment, we can take in a show at the Astor Theatre - the oldest performing arts centre in Nova Scotia.</p>

						<h2>Day Seven:  Annapolis Valley, Nova Scotia</h2>
						<p>Traditional folk songs in Nova Scotia often mention leaving and wanting to return to the homeland.  The last day of our journey will encompass not so much music, but some of the lives and lifestyles which played a large role in our musical heritage.  We'll begin by travelling inland to a working heritage farm museum then head to the shore to visit a rustic fishing village, where we'll lunch and enjoy what the ocean provides.  Our trip 'down the mountain' will allow for spectacular scenery and perhaps a visit Grande Pre - a UNESCO world heritage site and an important part of Acadian history.</p>

						<p>Our final stop will be in the tiny village of Avondale, which was once a bustling shipbuilding town and where many a man went to sea - typical of so many small, coastal communities in the Province which are frequently written in song.  Here we will also visit a small, boutique winery where we can kick back and enjoy the high-notes of this musical journey.</p>

						<p>We'll return to Halifax in time for your evening meal and perhaps a concert, show or 'live-gig' at a venue of your choosing.</p>
						 
