						<h2><strong>Day 7 -  Nova Scotia Coastal Tour</strong></h2>
						<p> Leaving  Cape Breton, we'll travel back to the mainland and head to the Sunrise trail -  a scenic trailway running along the Northumberland Straight coastline of Nova  Scotia. Our destination is the coastal community of Malagash, home to  Nova Scotia's largest and oldest winery, <strong>Jost Vineyards</strong>.  With wines for every occasion and winning <em>hundreds of national and  international awards</em> you are sure to enjoy the Jost Experience!  Following a tour of the vineyard and sampling in their tasting room, why not  grab a picnic basket, bottle of wine and enjoy a picnic among the vines?  If you're feeling invigorated from the fresh sea air, hop on a bike, explore  the trails along the coastline in search of that perfect, secluded picnic spot!  Enjoy the sea air, peace and serenity. </p>
						<p>We'll be  back in Halifax for your evening meal and a chance to explore the Halifax  nightlife. </p>
						<div class="well">
