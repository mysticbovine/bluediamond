						<p>  We'll begin the day by visiting a heritage property which has  been transformed into an award winning winery<strong>. </strong>At <strong>Magnetic Hill  Winery</strong>, you'll discover why lovers of good wines are drawn to Magnetic  Hill for more than the optical illusion of cars rolling uphill without  power! As you sip and swirl, enjoy the gorgeous, panoramic views of the  Moncton area. </p>
						<p> No visit to New Brunswick would be complete without visiting the  Bay of Fundy and witnessing the world's highest tides. On the way to our  next winery, we'll stop at Hopewell Rocks where you can walk on the ocean  floor, stroll among the flowerpots and experience this true wonder of  nature. A scenic drive will take us to <strong>Waterside Farms Cottage Winery, </strong>New Brunswick's first cottage winery. Situated on the Bay of Fundy,  this award winning winery specializes in wild blueberry wines, a true taste of  New Brunswick. <br />
						Our return trip to Moncton will take us through Fundy National  Park which offers exceptional sightseeing and numerous activities and if our timing  is right, we may even catch a traditional kitchen party!</p>
						<div class="well">
						  <p>Options: Visit Cape Enrage; Rent a canoe, boat or kayak; Explore  caves or hike some trails; Tour fine art and craft studios; Visit Magnetic Hill  and all the attractions it offers.</p>
						</div>
						<img src="../../images/maritimes/matos-winery.jpg" class="img-thumbnail" alt="Matos Winery Sign with the winery in the background.">
						<h2><strong>Day 4 - Prince Edward Island</strong></h2>
						<p>  Leaving Moncton, we'll drive along the Petitcodiac River Valley  to visit <strong>Verger Belliveau Orchards. </strong>With over 70,000 apple  trees and a long, interesting agricultural history, we're sure you'll enjoy a  tour of the orchard and sampling a selection of their wines and ciders. <br />
						Continuing our drive to the Gentle Island, we'll visit <strong>Winegarden  Estate Ltd.</strong>, the only winery and distillery in the Atlantic Provinces.<strong> </strong>We'll  tour the winery and distillery, visit the wine cellar and museum and see the first  bottle of authentic German-New Brunswick Apple Schnaps made in Canada!  Perhaps enjoy a picnic on the estate or browse their wine store, offering over  70 varieties of grape wine, fruit wine, liqueurs and distilled spirits - all  made on site. </p>
						<p> Confederation  Bridge is a short drive away and will lead us onto Prince Edward Island.  We may stop at picturesque Victoria-By-The-Sea, a charming, historic, sea coast  village, on our way to <strong>Matos Vineyard and Winery. </strong>Following a tour  of the winery, we'll sip and swirl some of their selection at the tasting bar,  before heading to our accommodation at Brackley Beach. Although there will be a  variety of options for dinner, we suggest a traditional lobster supper, a treat  not to be missed! </p>
						<div class="well">
						  <p>Options:  Add more wineries; Incorporate shopping in Moncton or PEI; Relax on the beach -  you're on the Gentle Island - time to take things slowly!</p>
						</div>
						
						<img src="../../images/maritimes/rossignolwinery.jpg" class="img-thumbnail" alt="Rossignol Estate Winery main building. Sunny day.">						
						<h2><strong>Day 5 -  PEI Beaches to Cape Breton Highlands</strong></h2>
						<p> Our day  will begin in Charlottetown, Birth Place of Confederation. We'll tour  this quaint, capital city prior to visiting <strong>Newman Estate Winery. </strong>Situated  in a tranquil, rural location we'll tour the young vineyards, perhaps meet the  owner and sip his selections in the tasting room, which has been described as,  relaxed and comfortable, like sipping in a neighbor's kitchen. </p>
						<p>A short  drive will take us to <strong>Rossignol Estate Winery </strong>offering wines as unique  and beautiful as the Island. Tour the panoramic vineyard and savour the  flavour of selections such as Blackberry Mead Wine or Wild Rose Liqueur. </p>
						<p>Continuing  with tranquility, we'll take a ferry ride, crossing the Northumberland Strait,  to Nova Scotia. Relax and enjoy the spectacular scenery, spot some  porpoise and enjoy the sounds of Maritime Music drifting across the waves. </p>
						<p>On  arrival in Nova Scotia, we'll drive directly to Baddeck and spend the evening  in the charming Auberge Gisele's Inn. Enjoy an award winning dinner while  overlooking the beautiful Bras d'Or Lakes in Cape Breton. Life is good. </p>
						<div class="well">
						  <p>Options:  Spend an additional night in PEI and tour the Island, relax on the beach, take  in attractions such as Anne of Green Gables or a musical at Confederation  Centre of the Arts; Visit PEI's Vodka distillery; Spend the night in Pictou, Birth Place of New Scotland, tour the Hector Heritage Quay and learn of our  Scottish roots - your vacation, your way.</p>
						</div>
						<img src="../../images/maritimes/EileananBreaghaWinery.jpg" class="img-thumbnail" alt="Grapes growing on a sunny day.  A lake can be seen in the background.">
						<h2><strong>Day 6 -  Cape Breton </strong></h2>
						<p> Upon  awakening on the Bras d'Or Lakes, you may understand why Alexander Graham Bell  was enchanted by the beauty of Cape Breton and thought it 'out rivaledany  other place in his global travels'! We'll begin our day by visiting the Alexander  Graham Bell National Historic Site, en-route to Cape Bretons only winery, <strong>Eileanan  Br&egrave;agha Winery. </strong></p>
						<p>As Nova  Scotia's northernmost estate winery, Eileanan Br&egrave;agha (Scottish Gaelic for  <em>beautiful islands</em>), the vineyard was named in homage to the owner's Gaelic  heritage and for the majestic view of the numerous small islands in the Bras  d'Or Lakes. Enjoy, what has been said to be, "the most stunning  vineyard vista in the Province" with a little island in every glass!" </p>
						<p>Wine  making in Cape Breton is relatively new, but tea drinking is a long standing  tradition! We'll take a scenic drive through Cape Breton and pop into  Rita MacNeils Tea room to enjoy some true island tea hospitality before we  travel back in time and visit Fortress Louisbourg, a National Historic  Site. We'll return to Baddeck in time for dinner and a relaxing evening. </p>
						<div class="well">
						  <p>Options:  Add a day/night to Cape Breton and tour the Cabot Trail; Visit Canada's only  Single Malt Distillery; Explore the Gaelic and Acadian heritage of Cape Breton;  Whale watching and hiking; Museums and music. It's a small island with a lot  to offer - you choose what you want to see, do and experience.</p>
						</div>
