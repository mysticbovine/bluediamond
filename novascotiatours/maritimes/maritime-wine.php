						<h2><strong>Day 2 - New Brunswick inland river valley</strong></h2>
						<p>  Following our ferry ride from Nova Scotia, we'll head inland to  the Kingston Peninsula to tour <strong>Dunham's Run Estate Winery. </strong>Sample  a selection of reds and whites produced from their vineyard and perhaps also  taste their mead, cider and port.</p>
						 <p>We'll continue our drive inland to the head of Belleisle Bay  where we'll explore the award winning <strong>Gillis of Belleisle Winery.</strong>  Nestled in a beautiful river valley with stunning views down the bay, we're  sure you'll enjoy the unparalleled scenery while sampling a selection of unique  wines.</p>
						 <p>Our next stop will be on the shores of Washademoak Lakein  the small town of Cambridge-Narrows, to visit <strong>Mott's Landing  Vineyard. </strong>We'll stroll through their vineyards, learn their  history (and perhaps hear a story or two) and enjoy a sampling of their wines,  including their popular Cranpagne - a sparkling cranberry wine. </p>
						<p> Our evening will be spent in Moncton in a beautiful hotel on the  banks of the Petitcodiac River. Relax, unwind and enjoy views of the  famous Bay of Fundy.</p>
						<div class="well">
						  <p> Options: Explore the city of Saint John; Visit Reversing Falls;  Add more wineries or perhaps visit a cider company - the choice is yours.</p>
