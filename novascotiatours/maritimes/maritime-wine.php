						<p> From Falmouth, travelling across country, we'll stop in Gaspereau  Valley to visit <strong>Gaspereau Vineyards </strong>- asmall,  boutique winery showcasing some of the best wines Nova Scotia has to offer.  Once a dairy farm and apple orchard, this 50 acre property has been transformed  into a vineyard and passionately produces wine. Perhaps pop on a bike and  cycle through the vineyards or relax in the restaurant and enjoy some local  fare and superb wine. </p>
						<p> A short drive will take us through the University town of  Wolfville to Grande Pr&eacute;, where we'll visit one of the oldest wineries in Nova  Scotia, <strong>Domaine De Grand Pr&eacute;</strong>. This gorgeous winery not only  produces award winning wines but also boasts an award winning restaurant!  With the Bay of Fundy as a back drop, explore this beautiful vineyard, tour  their wine museum then sit back, sip and savour the flavours of their superb  wine and cuisine. </p>
						<p> Leaving Grande Pr&eacute;, we're travel to the southern area of the  Annapolis Valley to our evening accommodation, Digby Pines, in the town of  Digby. Enjoy a relaxing evening on the coast, dine on world famous Digby  Scallops and embrace the good life.</p>
