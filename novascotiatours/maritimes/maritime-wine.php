						<div class="well">Options: Add more wineries; remove some wineries; visit  museums; visit a UNESCO World Heritage Site; hike; visit spectacular  'look-offs' or a rustic fishing village for a meal of lobster. We can add  a day and spent some time exploring the town of Annapolis Royal (home to two  National Historic sites and beautiful heritage gardens) or even stay in a  rustic lodge and go whale watching.</div>
