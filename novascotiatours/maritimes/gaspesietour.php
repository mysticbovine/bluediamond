              <h3>Day 1</h3>
              <p>Departing from Halifax, we’ll follow the main highway to Moncton, New Brunswick where a swift right turn will take us onto the scenic ‘Starfish Trail’.  Following this trail, we’ll meander along the eastern coastline of New Brunswick, through quaint villages and towns such as Bouctouche (named ‘one of Canada’s most beautiful small towns’) and through the Kouchibouguac National Park, until we reach our destination for the evening, Miramichi – the largest town in Northern New Brunswick.  Relax in a hotel on the banks of the Miramichi and indulge yourself in some fine dining while watching the sunset over the Centennial Bridge….it’s a little bit of blissfulness!
              <h3>Day 2</h3>
              <p>Following breakfast on the banks of the Miramichi, we’ll continue along the eastern NB coastline, stopping for lunch in Pokemouche at Restaurant de la Poissonnerie du Creek.  This unassuming little restaurant will delight your taste buds!  We strongly suggest the clam poutine and a glass of house white wine!
              </p><p>After a satisfying lunch, we’ll head to Shippigan and begin our tour of the Acadian Isles – think of this as our “Island-Hopping” part of the adventure! 
              </p><p>We’ll pass over a few interesting bridges perhaps stopping at Miscou Beach for a wonderful stroll along the shore line (see if you can find the time difference on the beach;) and/or visit the spectacular peat bogs.  
              </p><p>We’ll culminate our tour of the Acadian Isles at Miscou Island Lighthouse. As they say “The road less travelled leads here... to shores of untouched, unspoiled and uncommon beauty.” This isolated and remote structure was built in 1856 at the very tip of the northeastern part of New Brunswick. The lighthouse “stands guard over the wild beauty of Miscou Island. Explore the windswept, sandy beaches while watching for birds and other forms of wildlife” and watch the waves as the Gulf of St. Lawrence meets the Bay of Chaleur - or you could relax and be less windswept in the restaurant and enjoy a cup of tea or bowl of soup….
              </p><p>We’ll back track off the islands and spend the evening in a charming Victorian hotel in Caraquet overlooking the Bay of Chaleur.
              </p>

              <h3>Day 3</h3>
              <p>As we continue on this coastline, you will see proud Acadian heritage everywhere so our first stop this morning will be at the Village historique acadien – a living museum portraying the daily lives of the Acadians from 1770 to 1949.
              </p><p>Following the Bay of Chaleur and NB coastline, we’ll head to Campbellton, see how many salmon you can spot (statues or otherwise), before we stroll along the banks of the Restigouche river with views of the J.C. Van Horne Bridge and Quebec.
              </p><p>We can either stop for lunch in Campbellton or continue to Saint-Omer to a super little French diner, Restaurant Le Vieux Marché.
              </p><p>The afternoon will be spent touring along the Bay of Chaleur, Quebec, coastline.  We may stop in Paspébiac – witness to two centuries of epic fishing history in Gaspésie, and the largest wooden structure in North America! Or perhaps Port-Daniel–Gascons city hall is more your kind of architecture?   We’ll see them both as we make our way to Percé which is where we’ll spend our evening, overlooking Percé Rock and Bonaventure Island.
              </p><p>Our hotel is within walking distance of numerous restaurants and sites, relax and enjoy your evening.
              </p>
              <h3>Day 4</h3>
              <p>Before we do anything today, a must is breakfast and we highly recommend the hotels buffet.  It has spectacular views and if you open the windows, you’ll hear the waves crashing…then indulge yourself in their Quebec bread pudding!  Give your senses a complete overload!
              </p><p>Now you’re full of food and your senses have gone wild, let’s switch things up! We’ve done a lot of driving to get here so it’s time for a change.  Jump on the boat and tour Percé Rock and stroll around Parc national de l'Île-Bonaventure-et-du-Rocher-Percé - home to the largest migratory bird refuge in North America.  It truly is an unforgettable experience.
              </p><p>We’ll lunch in Percé before our afternoon drive to Gaspé, perhaps stopping in Coin-du-Banc Beach to take a deep breath and marvel at the scenery.
              </p><p>The evening will be yours to stroll around the town of Gaspé, one of the oldest settlements in North America.
              </p><h3>Day 5</h3>
              <p>Leaving Gaspe, we’ll head to Forillon National Park.  Time may or may not allow for a hike but we’ll definitely visit the shoreline.  If you listen carefully, whales can be heard in the distance and numerous birds can be seen.  As we leave the park, we’ll drive through L'Anse-au-Griffon, keep your eyes open for interesting tributes to its fishing heritage!
              </p><p>Brace yourselves for some spectacular scenery and breathtaking views.  This is one of the most beautiful drives we have the pleasure of touring our visitors.  Make sure your camera batteries are charged!  We will undoubtedly stop at many places for scenery, but one place is a must on our itinerary - Centre d'Art Marcel Gagnon in Sainte-Flavie.  You will be enchanted by Le Grand Rassemblement, approximately 100 life-sized sculptures emerging from the St. Lawrence River It truly is a sight to behold.
              </p><p>We’ll spend the evening in Rimouski, situated along the banks of the St. Lawrence River.  Rimouski is a delight to visit on foot, but for an evening meal and something a little bit different, we suggest hopping in a taxi to the village of Bic.  Here you will find ‘Chez St-Pierre’, which according to ‘Canada’s 1oo Best’ is the #46 restaurant in Canada!  If you want a truly unique gastronomic experience where ”culture, history and tradition come alive”, we suggest let Chef Colombe Saint-Pierre rule your taste buds!  You will not be disappointed.
              </p><h3>Day 6</h3>
              <p>We’ll head back to New Brunswick following the highway along the Maine Border, along New Brunswick’s scenic River Valley Drive.  If you didn’t try a true Quebec Poutine before heading home, we’ll stop in the village of Sainte-Cyprien to satisfy your taste buds!
              </p><p>We will definitely stop in Waterville, New Brunswick so you can see (and experience) the worlds shortest and longest covered bridges and visit the Covered Bridges Chips factory.  We’ll spend the evening in Moncton where you can stroll along the board walk, visit craft breweries and perhaps even take in a show at the restored 1920s-era vaudeville house, Capitol Theatre.
              </p><h3>Day 7</h3>
              <p>No visit to this part of Canada is complete without seeing the Bay of Fundy.  We’ll head southwards to Hopewell Rocks, also called the Flowerpot Rocks.  If tides permit, this is your chance to walk on the Ocean floor – truly an amazing experience although somewhat muddy!  We’ll also stop in Truro so you can hopefully witness the tidal bore phenomenon.  We’ll visit the information centre so you can understand how unique and impressive the bore can be.
              </p><p>We’ll be back in Halifax in time for your evening meal!

              </p><p>Wow!  What a trip with a scenic sensory overload!  As with all Blue Diamond Tours, this is just a suggestion.  We can fully customize any trip to your taste and interests. Contact us to start planning your trip to Canadas East Coast!
              </p>
