						<div class="col-md-8">
						
						<p>Upon arrival in Halifax, we’ll meet you at the airport and
						transfer you to your hotel, giving a brief orientation of the area on the
						way. The remainder of the day is yours to relax.</p>
						
						<h2>Day 1 – Grande Pre and surrounding area</h2>
						<p><img src="../../images/maritimes/cross.jpg" class="img-thumbnail" alt="An old photo form 1924 of the Deportation cross in Hortonville, Grand-Pré, Nova Scotia."></p>
						<p>Grand Pre is a National Historic Site and a UNESCO World
						Heritage Site located on reclaimed shores of the Bay of Fundy and was once home
						to a large Acadian community. We will spend the morning exploring the
						history of Acadians and their expulsion. The site has a superb visitor’s
						centre with interactive displays and an interesting multimedia
						presentation. Following a visit to the centre, stroll the expansive
						gardens, visit the memorial chapel and explore the nearby trails which offer
						stunning views of the Acadian landscape.</p>

						<p>A short drive or walk, will take us to the Deportation Cross.
						The cross was erected in 1924 and marks the site where Acadian deportees were
						loaded onto row boats and taken out to the transport ships anchored in the
						Minas Basin. Take some time to reflect and imagine the scene in 1755.</p>

						<p>Leaving Grand Pre, we’ll travel over Acadian Dykelands to
						the village of Port Williams where we’ll lunch and enjoy the view across reclaimed
						lands. Following lunch and weather permitting, enjoy a short stroll
						across the dykes before a short drive to Blomidon Look-Off. Here,
						stunning panoramic views of the Minas Basin will captivate you!</p>

						<p>Prior to returning to Halifax, we’ll stop at Sainte-Famille
						Winery in Falmouth. The winery is located on land which was first settled
						by Acadians around 1685. Remains of the original church are still on the
						property today, although somewhat under water. We'll visit the cemetery
						and interpretive area which describes the deportation of these Acadians to the
						United States, then stroll through the scenic vineyard, tour the winery and
						sample a selection of their wines, perhaps pairing with local cheese for a true
						Nova Scotian taste.</p>

						<p>We’ll return to your hotel in Halifax where the remainder of
						the day is yours at your leisure.</p>

						<h2>Day 2 – Shore to Shore</h2>
						<p><img src="../../images/maritimes/lunenburg.jpg" class="img-thumbnail" alt="Old house in Lunenburg. Black and white photo."></p>
						<p>We’ll begin the day with a scenic drive along the South
						Shore of Nova Scotia, stopping in Lunenburg for a brief stroll around the
						quaint town. Founded in 1753, Lunenburg is a UNESCO World Heritage Town
						steeped in history and offers breathtaking architectural and ocean views.
						If we’re lucky, the famous Bluenose schooner may also be in port!</p>



						<p>Continuing our drive along the South Shore, our next port of
						call with be Fort Point Museum in LaHave. This National Historic Site
						celebrates the founding of &quot;Fort Sainte Marie de Grace&quot; in 1632, the
						first capital of New France. Enjoy a brief tour of this unique, community
						museum and savor the views which are as “picturesque as when Champlain
						discovered the area in 1604”!</p>



						<p>Leaving LeHave, we’ll head to Liverpool, which is where
						we’ll lunch, before travelling cross country to Port Royal National Historic
						Site. This site, features a reconstruction of the Habitation, where, in
						1605, Samuel de Champlain helped establish one of the first European
						settlements in Canada. Costumed interpreters will transport you back in
						time to help you learn about early European colonization and settlement, and
						the French settlers’ important relationship with the Mi'kmaq people. You
						may also be able to trace your Cajun roots here.</p>



						<p>Following our afternoon at the Habitation, we’ll head to Digby
						Pines which is where we’ll spend the evening.</p>



						<h2>Day 3 – Acadian Coastline</h2>
						<p><img src="../../images/maritimes/SteAnnes.jpg" class="img-thumbnail" alt="Universite Sainte-Anne, Church Point, Nova Scotia."></p>
						<p>Today, we’ll tour along the Acadian coastline and immerse
						ourselves in Acadian history and culture, beginning with a stop in the
						community of Church Point. Church Point is home to Université Sainte-Anne,
						the only French language university in Nova Scotia, and houses the Acadian
						Centre Archives. The university is also home to Rendez-vous de la Baie
						(Acadian Cultural and Interpretive Centre), a “state-of-the-art museum”
						featuring bilingual interpretive panels, artifacts and interactive multimedia
						displays which illustrate the story of the Acadians, past and present.
						Located next to the university is Église Sainte-Marie, a former church and the
						largest wooden structure in North America! It is now a registered museum and
						well worth a visit.</p>

						<p>We’ll stop for lunch in Yarmouth before heading to the
						villages of Pubnico – founded over 350 years ago and part of the oldest Acadian
						settlement in Nova Scotia. Located in West Pubnico in a c1800 homestead,
						is Musée des Acadiens des Pubnicos et Centre de recherche (Acadian Museum and
						Research Centre). Explore Acadian history dating back to 1653, tour a
						traditional Acadian garden and experience life as it was for our Acadian
						ancestors. The research centre has an extensive collection of historical
						and genealogical documents and over 6000 publications with information dating
						back to the 17th century.</p>

						<p>We’ll continue on to Lower West Pubnico where we’ll visit Le
						Village historique acadien de la Nouvelle-Écosse. Situated on 17 acres overlooking
						the harbour, this community is the oldest Acadian region still inhabited by
						descendants of the original founder. Explore this unique little village
						and learn how it survived the deportation.</p>

						<p>No trip to this area would be complete without a Musique de
						la Baie kitchen party experience! On our return to Digby, we may stop for
						dinner and a toe-tapping, musical evening at an Acadian kitchen
						party.</p>

						<p>We’ll return to Digby Pines for the night.</p>



						<h2>Day 4 – New Brunswick and the Bay of Fundy</h2>
						<p><img src="../../images/maritimes/saintjohn.jpg" class="img-thumbnail" alt="Old photo of a tall masted ship in the harbour of Saint John New Brunswick. A large crowd of people are on the dock."></p>
						<p>We’ll travel by ferry from Digby to Saint John, New
						Brunswick – make sure to have your camera on hand to capture some spectacular
						scenery and perhaps wildlife! From Saint John, we’ll drive to the Fundy
						National Park and if time allows, incorporate some of the stunning Fundy Trail
						Parkway into our route. Fundy National Park encompasses some of the last
						remaining wilderness in southern New Brunswick and hugs the coastline of the
						Bay of Fundy. We’ll spend some time in the village of Alma, on the shores
						of the world’s highest tides, where you can enjoy trails, sightseeing, shopping
						and lunch.</p>



						<p>We’ll continue along the coastline to Cape Enrage which
						offers spectacular clifftop views of the Bay of Fundy and the turbulent waters
						within.</p>



						<p>Meandering along the scenic Fundy Coastal Drive, we’ll reach
						Hopewell Rocks where we’ll stay and play for the afternoon. Walk on the
						ocean floor, explore the beauty and history of this area and learn of its
						importance through multi-media exhibits in the interpretive centre. It
						promises to be an awe inspiring experience.</p>



						<p>Our evening will be spent in Chateau Moncton on the banks of
						the Petitcodiac River where you can relax and unwind after the whirlwind of the
						day.</p>



						<h2>Day 5 – New Brunswick to Nova Scotia</h2>
						<p><img src="../../images/maritimes/wolfville.jpg" class="img-thumbnail" alt="View of the old town from a height. Trees and churches can be seen."></p>
						<p>The day will begin with a visit to Musée Acadien de
						l’Université de Moncton which features one of the largest collections of
						Acadian artifacts in the world. </p>



						<p>Leaving Moncton, a short drive will take us to Memramcook,
						known as the &quot;Berceau de l'Acadie&quot;, which translated means
						&quot;Cradle of<a href="https://en.wikipedia.org/wiki/Acadia"
						title="https://en.wikipedia.org/wiki/Acadia">Acadia</a>&quot; where we will
						visit Monument-Lefebvre National Historic Site. The exhibit, “Reflections
						of a Journey – The Odyssey of the Acadian People,” will take you on a journey
						into the past and future of the Acadian people. Also located on this site
						is the Boutique acadienne, a large boutique dedicated to selling Acadian
						products, ranging from paintings and caps to CD’s and rare books – you’ll find
						it all here!</p>



						<p>We’ll briefly stop in Sackville, NB for a bite to eat (or
						pick up a picnic) before we visit Fort Beauséjour – Fort Cumberland Historic
						Site located on the border of Nova Scotia and New Brunswick. Here, in
						this star-shaped fort,you’ll be transported back in time to an era when
						England and France were caught in a tug-of-war for supremacy in Acadia. Stroll
						the grounds and enjoy the views of the marsh, bay and surrounding countryside.</p>



						<p>As we enter Nova Scotia, we’ll turn off the highway and head
						towards Joggins Fossil Cliffs, a UNESCO World Heritage Site, on the Bay of
						Fundy. Located on an old coal mine, a superb fossil centre illustrates
						the rich geological history of the area through exhibits and displays.
						Join a guide on the shore and see if you can find a 300 million year old fossil
						or you may prefer to relax in the café and enjoy a coffee from Nova Scotia’s
						organic, fair trade company “Just Us”.</p>



						<p>We’ll return to Halifax for the evening.</p>



						<h2>Day 6 – Hants County Shoreline</h2>
						<p><img src="../../images/maritimes/hants.jpg" class="img-thumbnail" alt="The German cargo ship Sabine Howaldt in the tidal harbor from Walton, Nova Scotia, Canada - 1958"></p>
						<p>The Hants County shoreline is perhaps one of the best kept
						secrets in Nova Scotia! Not only is it home to the highest tides in the
						world, had numerous Acadian settlements but it also has a rich ship building
						history and offers stunning scenery around every turn.</p>



						<p>From Halifax, we’ll head to South Maitland to visit the Fundy
						Tidal Interpretive Centre. Learn about the power of the Fundy tides and
						the tidal bore – a unique phenomenon which can be seen from this location (if
						tide and times cooperate).</p>



						<p>Continuing along this shoreline with dramatic views abound,
						we’ll pass through the historic ship building village of Maitland, then briefly
						stop at Anthony Provincial Park in Lower Selma, to not only enjoy the view, but
						to discover its role in the development of the atomic bomb! </p>



						<p>We’ll enter the small village of Noel, named after Acadian
						Noel Doiron, who constructed the numerous dykes which still frame the shoreline
						and which we’ll cross as we head to Burncoat Head Park – home of the highest
						recorded tides in the world! Here, you can explore the lighthouse, trails
						and perhaps even walk on the ocean floor! The natural beauty of this area
						will captivate you, from sea stacks to soaring eagles, it truly is a sight to
						behold.</p>



						<p>From one lighthouse to another of a very different
						design. The Walton lighthouse is the oldest lighthouse in Hants County
						(built 1873) and is perched on top of cliffs overlooking the Bay of Fundy. A
						short walking trail will take you to some amazing views of the Bay of Fundy.</p>



						<p>Our next stop will be in the small village of Summerville at
						the Flying Apron Inn, where we’ll lunch and enjoy local, seasonal fare.</p>



						<p>To top off the tour, our last port of call will be in
						Avondale, a former Acadian community and thriving shipbuilding village, now
						home to a boutique winery. The Avondale Sky Winery is housed in a former church
						from Walton, which, only a few years ago, sailed on the Bay of Fundy, to its
						new home in Avondale. A big adventure for a little church! </p>

						<p>We’ll return to Halifax in time for your evening meal.</p>



						<p>Throughout this tour, we encourage you to ‘spot’ Acadian
						Heritage Markers along the roadside which commemorate an important site in
						Acadian history. There are approximately 40 on this tour. See you many you can
						find…</p>



						<p>As with all Blue Diamond Tours, this is tour can be tailored
						to your own personalinterests. Contact us for more information.</p>
							
						
