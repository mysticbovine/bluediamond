							<h2>Day 1: Halifax and Rugged Coastlines</h2>
							<p><img src="../../images/maritimes/peggyscove.jpg" class="img-thumbnail" alt="The rugged coastline of Peggys Cove."></p>

							<p>We will introduce you to this historic city by visiting the
							Citadel, Public Gardens, York Redoubt, Dingle Tower and in honor of
							Confederation, perhaps tour Province House. 2017 also marks the 100<sup>th</sup>
							anniversary of the Halifax explosion – let us tell you about it and explain why
							Nova Scotia sends a Christmas Tree to Boston every year.</p>

							<p>An afternoon drive will take us along the rugged coastline
							to visit Peggy’s Cove – an iconic landmark in Nova Scotia. If time
							allows, we will also visit the Swiss Air memorial and the quaint fishing
							village of Dover, before we head back to Halifax for the evening.</p>

							<h2>Day 2: Tides, Vines and Valleys</h2>
							<p><img src="../../images/maritimes/hallsharbour.jpg" class="img-thumbnail" alt="Fishing boats sit on the muddy floor of the harbour at low tide."></p>

							<p>Today will revolve around the tides and the spectacular Bay
							of Fundy. We will begin by visiting Hall’s Harbour at low tide (if timing
							allows), meander our way to Blomidon Look off, then down the mountain to
							explore the Valley floor. A visit to Grand Pre is a must so we can look at the
							life of our Acadian ancestors and learn of their sad history. We’ll visit
							the University town of Wolfville and perhaps enjoy a little shopping or cider
							tasting before we head to one of the numerous wineries in the area. A
							quick trip back to Hall’s Harbour will let you see the dramatic tidal range of
							the Bay of Fundy. Boats which lay in the mud earlier are now floating on
							the ocean.</p>

							<p>As we return to Halifax, we may detour through the town of
							Windsor and visit the British Blockhouse of Fort Edward and Haliburton House
							Museum – once home to Thomas Chandler Haliburton. Haliburton was a politician,
							lawyer, judge and author and played a significant role in Nova Scotia prior to
							its entry into Confederation.</p>

							<h2>Day 3, 4 and 5: Cape Breton Charm</h2>
							<p><img src="../../images/maritimes/brasdor-view.jpg" class="img-thumbnail" alt="Two red adirondack chairs still on a grassy hill overlooking the Bras D'Or Lake."></p>

							<p>Leaving Halifax, we’ll travel the major highway to Cape Breton
							with a stop en-route at the Millbrook Heritage Centre to explore the
							fascinating history of our First Nations. Our base in Cape Breton will be
							in Baddeck, from which we can explore this beautiful part of Nova Scotia. The
							Cabot Trail, Ceilidh Trail and Fortress Louisbourg are all on our
							itinerary. Perhaps we’ll find a Celtic Kitchen Party or sail on the Bras
							d’Or Lakes…or both. Relax and let yourself ‘be taken’ by the Cape Breton
							hospitality.</p>

							<h2>Day 6, 7 and 8: The Birthplace of Confederation</h2>
							<p><img src="../../images/maritimes/confederation-conference.jpg" class="img-thumbnail" alt=" Convention at Charlottetown, P.E.I., of Delegates from the Legislatures of Canada, New Brunswick, Nova Scotia, and Prince Edward Island to take into consideration the Union of the British North American Colonies."></p>

							<p>Leaving the rugged highlands of Cape Breton behind, our next
							port of call will be the town of Pictou - The Birth Place of New
							Scotland. Enjoy a short stroll through this quaint village, perhaps
							“adopt a lobster” or board a replica of the ship Hector, which once brought
							Scottish settlers seeking a better life. We’ll take the ferry to Prince Edward
							Island, the Birthplace of Confederation and affectionately known as the Gentle
							Island. Charlottetown is the capital of PEI and home to Province House
							where theCharlottetownConference of September 1864
							setConfederationin motion. It is a bustling little city, lots
							to see and learn. We’ll also get our feet wet and visit some of the
							gorgeous beaches PEI has to offer and of course, will learn about Anne of Green
							Gables. PEI has some amazing locally sourced produce – perhaps a visit to
							Malpeque for fresh oysters or to Deep Roots Distillery for <i>maple liqueur</i>…the
							choice is yours – let your palette explore and body relax.</p>

							<h2>Day 9: Confederation Bridge – PEI to New Brunswick</h2>
							<p><img src="../../images/maritimes/confederation_bridge.jpg" class="img-thumbnail" alt="The Confederation Bridge to Prince Edward Island."></p>

							<p>Opened in 1997, this 8-mile long bridge was named
							Confederation Bridge and connects Prince Edward Island to New Brunswick, it
							truly is a sight to behold. We’ll travel through New Brunswick and some
							of the awe inspiring Fundy National Park as we head to Saint John. Here,
							we’ll board the ferry and travel to Digby, Nova Scotia which is where we’ll
							spend the night. Be sure to step outside on the ferry and have your
							camera on hand, you never know what spectacular sights and views you might
							capture. After a whirlwind few days, today will offer a time to relax.</p>

							<h2>Day 10: Step back in time</h2>
							<p><img src="../../images/maritimes/fortanne150.jpg" class="img-thumbnail" alt="Fort Anne's Officer residence."></p>

							<p>Leaving Digby, we’ll visit Bear River “The Tidal Village on
							Stilts” before we reach Annapolis Royal which is where we will spend much of
							our day. We’ll step back in time and tour the Habitation, Fort Anne and
							historic gardens. Travelling across country, through the heart of Nova
							Scotia, we’ll spend the afternoon and evening in Liverpool. A beautiful
							town and home to two private museums owned by Sherman Hines – Rossignol Cultural
							Centre and the Sherman Hines Museum of Photography, both of which are worthy of
							a visit. From Hunting and Mi’Kmaq, Apothecary and Out House exhibits to
							Provincial photography, personal collections and a Yousuf Karsh
							collection. Both are truly an experience not to be missed and fun for all
							ages.</p>

							<h2>Day 11: South Shore</h2>
							<p><img src="../../images/maritimes/lunenburg150.jpg" class="img-thumbnail" alt="Lunenberg waterfront."></p>

							<p>Our first stop will be in the Town of
							Lunenburg – one of only two UNESCO World Heritage towns in North America. It
							was founded in 1753 and to this day, maintains its unique identity. Take time
							to explore its quaint streets, art galleries and stroll along the board walk –
							perhaps the Bluenose II will be in port for you to explore?</p>

							<p>Mahone Bay is a short drive away and has
							been described as one of Nova Scotia’s most romantic villages – it’s also a
							great place to find that perfect gift for someone and indulge yourself in one
							of its many cafes.</p>

							<p>Chester will be our final stop before
							heading back to the city. It is a beautiful, unique community which was a
							one-time fishing village and currently, home to the largest keelboat regatta in
							Canada. It is also home toThe Fo’c’sle – Nova Scotias oldest pub,
							dating back to 1764. Locally known as “Chester’s Living Room”, why not
							pop in for a drink, sit back and wonder what you would hear “if walls could talk”?</p>

							<h2>Day 12: Farewell</h2>

							<p>We will meet you at your hotel and transfer you to the
							airport. You came as strangers but will leave as friends and we hope to see you
							here again.</p>

							<p>As with all Blue Diamond Tours, this tour can be modified to
