<h3>Day 1</h3>

<p>Departing
from your hotel our first stop is to Alexander Keith's brewery Founded in 1820,
Alexander Keith's is a brewery in Halifax, Canada. It is among the oldest
commercial breweries in North America. Keith's was founded by Alexander Keith
who emigrated from Scotland in 1817, He was mayor of the city of&nbsp;<a
href="https://en.wikipedia.org/wiki/Halifax_(former_city)" target="_blank"
title="Halifax (former city)">Halifax</a>,
Nova Scotia, a Conservative member of the provincial legislature, and public as
a leader of the&nbsp;<a href="https://en.wikipedia.org/wiki/Freemasons"
target="_blank" title=Freemasons>Freemasons</a>.
He became Provincial&nbsp;<a
href="https://en.wikipedia.org/wiki/Grand_Master_(Masonic)" target="_blank"
title="Grand Master (Masonic)">Grand Master</a>&nbsp;for
the Maritimes under the English authority in 1840 and under the Scottish lodge
in 1845. Following a reorganization of the various divisions in 1869, he became
Grand Master of Nova Scotia. There are four masonic lodges named in his
honour:&nbsp;<a href="https://en.wikipedia.org/wiki/Moncton" target="_blank"
title=Moncton>Moncton, New Brunswick</a>, and
Halifax,&nbsp;<a href="https://en.wikipedia.org/wiki/Stellarton" target="_blank"
title=Stellarton>Stellarton</a>, and&nbsp;<a
href="https://en.wikipedia.org/wiki/Bear_River,_Nova_Scotia" target="_blank"
title="Bear River, Nova Scotia">Bear River</a>&nbsp;in
Nova Scotia.</p>

<img src="/images/novascotia/perfectbeer/image001.png" class="img-rounded">

<p>Our
second stop before leaving the city will be Garrisons brewery, Garrison opened
their doors in 1997. Their very first beer was an Irish Red, a beer that they
still brew and sell today. They&nbsp;have a strong presence in Nova Scotia and
a loyal fan base. It is what introduced me to craft beer, a story we often hear
repeated among locals. Everything brewed at Garrison is made without
preservatives. They boast the largest line-up of seasonal, special and
collaboration beer, and pride themselves on using only the finest natural
ingredients.</p>

<p>Leaving the hectic city behind us taking into view: Historic properties; Halifax
Harbour; the site of the 1917 Halifax Explosion; Halifax Citadel; the Titanic
Cemetery and we're off to Mahone Bay. We'll follow a scenic route known as the
&quot;Lighthouse Route&quot; which meanders along the South Shore of Nova
Scotia. As the name suggests, look for charming lighthouses along the rugged
shoreline and peninsulas to arrive at Mahone Bay.</p>

<p>Mahone Bay is a picture-perfect postcard town noted for its three churches -an iconic
Nova Scotian image. Mahone Bay is also home to a lovely harbour, specialty
shops and superb cafes and restaurants. Some say it's the finest romantic
weekend getaway spot in Nova Scotia! Take time to stroll through the streets
and enjoy some of Mahone Bay's charm, delicacies and offerings.</p>



<p>Here we'll visit Mahone Bays first craft brewery, The Salt Box. We'll stop into
their tap room and taste one of their "foundation beer",&nbsp;inaugural
small-batch recipes created or adapted by their brewer. The most popular of
their foundation editions will become regular offerings at Salt Box.</p>



<p>Our next stop is in the Town of Lunenburg - one of only two UNESCO World Heritage
towns in North America. It was founded in 1753 and to this day, maintains its
unique identity through amazing architecture (such as the Lunenburg Academy),
brightly painted buildings and breathtaking scenery.</p>

<p>Lunenburg is home port for the world-famous fishing schooner Bluenose II - a replica of
the Bluenose (A celebrated racing and fishing vessel and a Canadian icon). The
Bluenose II can often be seen in the bustling harbour along with many other
fishing boats - Lunenburg is known as the &quot;Fishing Capital of the
World&quot; and is home to the Fisheries Museum of the Atlantic.</p>

<p>We'll take a brief tour of the town including a stop at the historic, 250+ year old
Gothic Revival style St. John's Anglican Church. This lovely church was almost
completely destroyed by fire on Halloween in 2001, but has since been
completely and lovingly restored to its former grandeur. While here, we will be
able to view the church's copy of the Vinegar Bible, so called because it
contains a typographical error, substituting vinegar for vineyard in the Gospel
of Luke. Printed in 1717 by John Baskett, this particular Bible - one of three
with the typo - is older than Halifax.</p>

<p>While in Lunenburg we can keep the theme going and visit the Iron Works Distillery, A
Distillery creating artisanal spirits, housed in an 1893 heritage marine
blacksmith's workshop they take their name from.</p>

<p>We'll travel further down the south shore to a town founded in 1759, LiverPool.
Liverpool's harbour was an ancient seasonal camp of Nova Scotia's native&nbsp;<a
href="https://en.wikipedia.org/wiki/Mi%27kmaq_people" target="_blank"
title="Mi'kmaq people">Mi'kmaq</a>&nbsp;and
was known as Ogomkigeak meaning &quot;dry sandy place&quot; and Ogukegeok,
meaning &quot;place of departure&quot;.&nbsp;<a
href="https://en.wikipedia.org/wiki/Samuel_de_Champlain" target="_blank"
title="Samuel de Champlain">Samuel de Champlain</a>&nbsp;originally
named the harbour Port Rossignol, in honour of Captain Rossignol, an early
17th-century founder of New France in North America who used the harbour for
fur trading.<a
href="https://en.wikipedia.org/wiki/Liverpool,_Nova_Scotia#cite_note-1"
target="_blank">[1]</a>&nbsp;Later&nbsp;<a
href="https://en.wikipedia.org/wiki/Nicolas_Denys" target="_blank"
title="Nicolas Denys">Nicolas Denys</a>, a
pioneering 17th-century French explorer and trader of Nova Scotia, was granted
land here by the leader of Acadia, Isaac de Razilly&nbsp;</p>

<p>Here we will bed down for the night but with one more brewery to visit, Hell Bay
Brewing company. A chef by trade, Marc Baillie started brewing Hell Bay beer as
a hobby, but it wasn't long before a local restaurant and then his whole
community caught on. Pretty soon, that hobby was taking over most of a large
shed at his home in Cherry Hill, and demand for his beer throughout the
community was growing. In spring of 2013, Hell Bay opened their current brewery
on the Liverpool waterfront. In addition to their two-standard beer, Hell Bay
regularly releases seasonals like scotch ale and dark cream ale.</p>

<img src="/images/novascotia/perfectbeer/image003.png" class="img-rounded">

<p>We'll head to the "Quarter Deck", Villa and loft style hotel rooms, right on the
beach with fantastic views, Let the waves put you to sleep this night.</p>

<h3>Day 2</h3>



<p>Following breakfast, we are as the famous song says "On the road again" further down the
south shore, depending on your interests we can visit Crescent Beach is a
mile-long stretch of white sand that links the town of Lockeport to the
mainland. This beautiful beach was once featured on our fifty-dollar bill and
is a sight to behold.</p>

<img src="/images/novascotia/perfectbeer/image005.png" class="img-rounded">

<p>From here we will continue on our first brewery of the day Boxing Rock. After
spending a few years testing their beer&nbsp;on a willing group of friends,
engineers Emily and Henry settled on three inaugural beers and launched Boxing
Rock Brewery. We're glad they did. Boxing Rock has quickly become one of the
top selling breweries in the province. They've kept their flagship beer, Hunky
Dory Pale Ale, Vicars Cross DIPA, and Temptation Red, and accompany it with a
regular rotation of seasonals</p>

<p>A short drive from the brewery is Shelburne, A destination not to be missed on
the famous Lighthouse Route.</p>

<p>While the influence of our Loyalist heritage is still felt in their county. Shelburne
Harbour has the third best natural harbour in the world. The native Mi'kmaq
traversed the Roseway River and used the surrounding lands for summer
encampments long before our shores were visited by Spanish, Portuguese and
French fishermen. Many of Shelburne's buildings date back to Loyalist times. Maybe
while we are taking in historic Water Street a photo stop at a giant andorak
chair.</p>

<p>We'll head for Yarmouth next but taking a stop at the Eel Lake Oyster Farm, a
family-owned business that offers two unique brands of oysters. The Ruisseau
Oyster is nurtured in Eel Lake, a tidal lake with approximately 2.5 per cent
salinity, and The Salt Bay Oyster, which is grown out in an ocean lease and
offers full salinity at 3.5 per cent.</p>

<p>Taste them both and you can decide on your favourite!&nbsp;</p>

<p>On to Yarmouth , Originally inhabited by the&nbsp;<a
href="https://en.wikipedia.org/wiki/Mi%27kmaq_people" target="_blank"
title="Mi'kmaq people">Mi'kmaq</a>, the region
was known as &quot;Keespongwitk&quot; meaning &quot;Lands' End&quot; due to its
geographic isolation being located at the southwestern tip of the&nbsp;<a
href="https://en.wikipedia.org/wiki/Nova_Scotia_peninsula" target="_blank"
title="Nova Scotia peninsula">Nova Scotia peninsula</a>.<a
href="https://en.wikipedia.org/wiki/Yarmouth,_Nova_Scotia#cite_note-3"
target="_blank">[3]</a><a
href="https://en.wikipedia.org/wiki/Yarmouth,_Nova_Scotia#cite_note-Names-4"
target="_blank">[4]</a></p>

<p>The region may have possibly been visited by&nbsp;<a
href="https://en.wikipedia.org/wiki/Leif_Ericson" target="_blank"
title="Leif Ericson">Leif Ericson</a>. An
object known as the&nbsp;<a
href="https://en.wikipedia.org/wiki/Yarmouth_Runic_Stone" target="_blank"
title="Yarmouth Runic Stone">Yarmouth Runic Stone</a>&nbsp;was
found at the nearby village of&nbsp;<a
href="https://en.wikipedia.org/wiki/Overton,_Nova_Scotia" target="_blank"
title="Overton, Nova Scotia">Overton</a>&nbsp;in
1812. It was interpreted by some to have been carved by Ericson, while others
feel the markings are natural scratches gradually enhanced over the years. The
stone is preserved at the&nbsp;<a
href="https://en.wikipedia.org/wiki/Yarmouth_County_Museum_%26_Archives"
target="_blank" title="Yarmouth County Museum &amp; Archives"><span
style='color:black'>Yarmouth County Museum &amp; Archives</a>. Also
through the 19th century the town was a major&nbsp;<a
href="https://en.wikipedia.org/wiki/Shipbuilding" target="_blank"
title=Shipbuilding>shipbuilding</a>&nbsp;centre,
at one point boasting more registered tonnage&nbsp;<a
href="https://en.wikipedia.org/wiki/Per_capita" target="_blank"
title="Per capita">per capita</a>&nbsp;than
any other port in the world.<a
href="https://en.wikipedia.org/wiki/Yarmouth,_Nova_Scotia#cite_note-9"
target="_blank">[9]</a>&nbsp;From 1874 to
1885, Yarmouth was the second largest port of registry in Canada
following&nbsp;<a href="https://en.wikipedia.org/wiki/Saint_John,_New_Brunswick"
target="_blank" title="Saint John, New Brunswick">Saint
John, New Brunswick</a>. In 1878, Yarmouth's tonnage peaked at 453
vessels aggregating 166,623 tons, and in 1879, the town had the second largest
registered tonnage in Canada.&nbsp;</p>

<p>As we take in the sights we'll be on our way to visit our second Brewery at
Rudder, A ten-barrel gravity flow brew pub located in Yarmouth, Nova Scotia.
They are the longest running brewery on the South Shore. Rudders makes
handcrafted full-bodied English style craft ales using fresh malted barley,
hops, spring water and fermenting yeast. All of their beer is unfiltered and
naturally carbonated, with no preservatives. They also have an onsite
restaurant if you are feeling peckish.</p>

<p>Before leaving Yarmouth behind us we can visit our Third Brewery, Heritage Brewery,
unique flavours hop and grains used by this brewing company. Founded in 2017 by
4 friends, they join a long held Maritime brewing tradition by making great
batches of high-quality craft beer. Small batches big taste as they say.</p>

<img src="/images/novascotia/perfectbeer/image007.png" class="img-rounded">

<p>Next to the town of Digby, the town where Maud Lewis lived with her Aunt and where
we'll spend the night. Take some time to explore the town and perhaps try some
world-famous Digby Scallops in the home of the world's largest scallop fleet.
Depending on your interests we can further explore the light of Maud Lewis
before bedding down at the Digby Pines and Resort.</p>

<h3>Day 3</h3>

<p>Following breakfast, we'll have a busy day today with four breweries and of course we are
going through wine country so the option is yours if we want to work in a
winery or two?</p>

<p>Our first venture is for Brier Island for some spectacular views and some
introductory to the Nova Scotia Side of the Bay of Fundy, quite literally this
is the beginning of the fundy coast line for N.S.</p>

<p>Brier Island is one of those special places where you go and become totally lost in
the beauty and tranquility around you.&nbsp; This small island is home to
whales, seabirds, flora, fauna, shipwrecks, lighthouses, hiking trails and
about 200 people.&nbsp;</p>



<p>Truly
a piece of paradise</p>

<img src="/images/novascotia/perfectbeer/image009.png"  class="img-rounded">



<p>Next,
we'll back track and visit our first brewery today at Roof Hound Brewing Co.
Owners Les and Jen Barr took first place in the India Pale Ale, or IPA,
category – the largest of the competition – at the fourth annual Atlantic
Canadian Beer Awards, held October 20 2017 at The Stubborn Goat in Halifax.</p>

<p>Their
beer called Big Stink IPA was chosen for top prize out of 45 entries. It's
become a flagship beer at the microbrewery, and not without an ironic twist.</p>



<p>Leaving
Digby, we off to the valley. The Annapolis Valley is noted world-wide for its
fruit trees, particularly apple trees, although there is also pear, plum,
grape, cherry, assorted berries and vegetables farmed here. It is true the
Acadians had apple orchards but it was horticulturalist Charles Ramage
Prescott, who, from 1811 to 1859, developed the apple industry in Nova Scotia.</p>



<p>We'll
be stopping at Lunn's Mill, in 1760, this beautiful part of the Annapolis
Valley was known as Lunn's Mill, named after the major industry in the area: a
bustling sawmill owned by John Lunn. Around this time, the Charming Molly set
sail from New England carrying the first New England Planters. These intrepid
people helped expand the community with farms and shops and in 1822 it was
renamed Lawrence town.</p>

<p>They
have 10 rotating taps of local craft beer and cider. Most beer is brewed on
site, but we also feature other great selections from around Nova Scotia. Try a
sampler flight.</p>

<p>Leaving
Lunn's Mill our next brewery is Bad Apple Brewery in Berwick, 10 different
varieties to try and they are barrel aged!&nbsp;</p>

<img src="/images/novascotia/perfectbeer/image011.png"  class="img-rounded">

<p>Next
up Sea Level Brewing in Port Williams, also a great place to view the tidal
phase of the Bay of Fundy. Sea Level Brewing is a micro-brewery, Randy
Lawrence, owner and brew master, opened Sea Level in 2007 after more than 20
years in the craft beer industry. In addition to the regular line-up, Sea Level
makes various seasonals (probably better referred to as bi-monthly brews), so
there is almost always something new to try. In addition to growlers, they can
a range of their beer line-up.</p>

<img src="/images/novascotia/perfectbeer/image013.png" class="img-rounded">

<p>Next
stop, tide times permitting is Hall's Harbour is named after Samuel Hall, an
American privateer who, during the American Revolution, used the cove to raid
settlements in the Annapolis Valley. Now it's home to a Bay of Fundy fishing
fleet and one of the largest lobster pounds in the world. Its unique size makes
it an ideal place to fully witness the startling wonder of the Fundy tides. Be
sure to capture a picture of what the harbour looks like now and compare it
with the view you'll see upon our return.</p>

<p>From
Hall's Harbour, we'll drive over the North Mountain Ridge to the Blomidon
‘Look-Off' where, on a clear day, you can see the Bay of Fundy, Grand Pré,
Wolfville and the extensive dykelands built by the Acadians. It is perhaps, one
of the most scenic vistas in Nova Scotia.</p>

<p>Following
our drive &quot;down the mountain&quot;, we'll visit Grand Pré National
Historic site to learn the history of the 1755 Acadian deportation. This
beautiful site and interpretive centre recalls the sad story of the deportation
and illustrates the history of a courageous people whose culture and actions
continue to enrich Canada.</p>



<p>We'll
make our way back to the university town of Wolfville and bed down for the
night.</p>





<h3>Day 4</h3>



<p>Our
last day of touring, we'll leave Wolfville behind us and travel to Windsor,
Nova Scotia, once home to industry and currently an agricultural hub is known
as the birthplace of Hockey to many and home of the pumpkin yacht races in the
end of the harvest season of October.</p>



<p>Our
first brewery is an interesting one, School House Brewery, named after the
building it resides in an old school house no longer used past the 1940's and
ironically owned and operated by two retired teachers whom had a passion for beer.
10 BBL, copper clad brew house along with several stainless Unitank Fermenters.
This allow our Brewers to ferment, cool, carbonate and package our product
efficiently in house! All recipes and Schoolhouse brews are produced here by
our self-taught "Brew Crew".</p>

<img src="/images/novascotia/perfectbeer/image015.png" class="img-rounded">

<p>Next,
we're off a short distance to Ashdale to&nbsp;<a
href="http://www.meanderriverfarm.ca/" target="_blank">Meander
River Farm and Brewery&nbsp;</a>. Meander River makes craft beer and
cider on their long-time family farm using hops grown on-site. Continuing the
farm philosophy of sustainability and environmental responsibility, Meander
uses their&nbsp;spent grain as compost and feed, and excess water from the
brewery for irrigation of their hops.&nbsp;They are the first Nova Scotia
brewery to offer a community shared brewery program.</p>



<p>As
we get close to Halifax it only makes sense to go off track a little to the
Off-track brewing company in Bedford. While they typically play in the area of
pales, IPAs and easy drinking wheats and browns, we will also venture into the
world of stouts, kolschs and many others.</p>

<img src="/images/novascotia/perfectbeer/image017.png" class="img-rounded">

<p>Our
last stop before returning you to your down town core accommodations is the
Good Robot Brewing company; as they put it "a brewery, bar, and beer garden run
by a pack of misfits in the heart of Halifax's historic North End", In the
summer the host a whole suite of odd ball events and even have a putting green
in their beer garden, what a way to end a great tour.</p>



<p>We'll
return you to your hotel with perhaps some directions to the thirty plus bars
and pubs in the down town core or if you're still craving the craft perfection,
one last tasting room one block away from your hotel called Propeller Brewing
company.</p>


