              <p>Maudie tells the story of a disabled arthritic women who unwittingly 
              became a beloved folklore artist in Nova Scotia.  Blue Diamond Tours would 
              like to take you on a tour to familiarize yourself with this Nova Scotian 
              artist, see where she lived, explore her community and fall in love with her… 
              and her Province.</p>
              
              <p>Upon arrival, BDT will collect you from the airport and transport you to 
              your hotel with a familiarization tour en route. During your first morning in 
              Halifax, we encourage (and can accompany you if you wish) to explore the Art 
              Gallery of Nova Scotia where you will find the original Maud Lewis home, replicas 
              of her artwork and her life story….as well as many other fascinating exhibitions.</p>
              
              <p>After lunching in the city, we’ll meander our way to the Annapolis Valley where 
              we can visit wineries, see the famous Bay of Fundy and explore some intriguing 
              history.  We’ll continue through the Annapolis Valley to the town of Digby, the town 
              where Maud lived with her Aunt and where we’ll spend the night.  Take some time to 
              explore the town and perhaps dine on some world famous Digby Scallops.</p>
              
              <p>Following breakfast, we’ll head to Annapolis Royal and take some time to explore 
              this gem of a town, perhaps visiting the Heritage Gardens and Fort Anne and also 
              learn about our Acadian and Mi’kmaq history.  Following a scenic trail, we’ll pop 
              into Bear River, a picturesque tidal village with buildings on stilts.  It is also 
              home to many artisans, a winery and a gorgeous waterfront Peace Park.</p>
              
              <p>Leaving Bear River, we’ll head to Marshalltown, the village Maud Lewis lived in 
              for 32 years.  We’ll visit the location of the original homestead, see the steel 
              memorial sculpture inspired by the house and visit a replica of the house and its 
              interior. The afternoon and evening will be spent exploring Mauds community, getting 
              to know it.  We’ll stay in Digby for the night.</p>
              
              <p>We’ll begin our return trip to Halifax with our first stop at Gilbert’s Cove 
              Lighthouse in Plympton – “The Greatest Little Lighthouse in Canada”.  Following 
              the coastline, we’ll pop into Church Point to see the tallest wooden church in 
              North America, pass through South Ohio, the birthplace of Maud Lewis then stop for 
              lunch in Liverpool.  We’ll visit the Rossignol Cultural Centre where there is a 
              wonderful replica of Mauds house and also many fascinating collections.  Continuing 
              along the coastline, we’ll stop in Lunenburg, a UNESCO world heritage site and home 
              to the Bluenose II.  We’ll return to Halifax in time for your evening meal.</p>
