<p>Nova Scotia is a stunning Province and we think it has some of the freshest, tastiest seafood in the world...so we thought we should create a tour with a perfect marriage– scenery and seafood!  Not only do we think it will tickle your fishy taste buds, but it will also provide a sight sensation.  So if you're a seafood nature lover, this is the tour for you!</p>
<img src="../../images/novascotia/seafoodtrail/seafoodtrail_1.jpg" class="img-rounded">
<h3>Day 1: Downtown Diner to Oceanfront Dinner</h3>

<p>We'll start the day by breakfasting at an iconic downtown Halifax eatery– the Bluenose II Restaurant.  (We strongly suggest the ‘Lobster Benny' to begin your day).  We'll then meander our way to the Annapolis Valley, perhaps stopping at a British Blockhouse, some wineries, breweries or distilleries and taking a well needed walk through some historic Acadian sites.  We'll head up the mountain for some stunning scenery at Blomidon Look-Off and perhaps take a walk on the ocean floor before we head to Halls Harbour to lunch on fresh on lobster at Halls Harbour Lobster Pound.  Sit back, relax and indulge your inner seafoodie!  It might be time for a wee afternoon nap!  Don't worry, we'll wake you when we arrive at Annapolis Royal.  Spend the afternoon wandering around this gorgeous, historic town.  Visit Fort Anne, historic sites and houses, smell the roses at the Gardens or simply stroll along the boardwalk, the choice is yours! A short drive will take us to Digby which is where we'll spend the night, at Digby Pines.  We encourage you to devour the famous Digby Scallops!</p>
<img src="../../images/novascotia/seafoodtrail/seafoodtrail_2.jpg" class="img-rounded">
<h3>Day 2: South Shore Indulgence</h3>

<p>Breakfast is at the hotel, be careful not to overindulge yourself...we have a lot to do today!  Following breakfast, we'll meander down the old road to Yarmouth, taking in some wonderful seascapes.  We may stop at inland sites such as the park dedicated to beloved Folk Artist, Maud Lewis, or see one of the largest wooden structures in Canada in Church Point, before heading along the coastline to Port Maitland, an ideal spot for a wee stroll along the boardwalk and beach.  We'll visit Cape Forchu lighthouse (remember to wave at the NS webcams) before stopping at Rudders in Yarmouth for lunch.  In our opinion, Rudders has absolutely fabulous fishcakes!  Next we'll visit Eel lake Oyster Farm often described as the real deal, meet the people who farm the oysters, and enjoy seeing their operation and buy some to take home or enjoy in their lake front eating area. Next we'll zig-zag between the highway and coastal route towards Lunenburg.  Just outside Lunenburg lies a fabulous short walk that is sure to heighten your senses – the Ovens.  It is a superb cliffside trail that is sure to excite and intrigue you with its history, sight and sound! We'll have an early dinner at the Old Fish Factory in Lunenburg – perfectly pair your local wines with fresh seafood and a view!  We'll back track slightly to Liverpool and end our day with a sunset evening stroll on a mile of white sand, lapping waves, and sea fresh air at the QuarterDeck Beachside Villas.</p>
<img src="../../images/novascotia/seafoodtrail/seafoodtrail_3.jpg" class="img-rounded">

<h3>Day 3: Cape Breton Bound...</h3>

<p>We've over indulged slightly and travelled a lot, today we'll take it easy.  Following breakfast at your hotel, we'll travel to Maitland, perhaps via Caddell Rapids, a very under estimated and under visited provincial park.  If timed correctly, this viewing area can provide the most spectacular views of the tidal bore and the road out can often offer an interesting ride – did we mention that we like "off the beaten track?"  Not to worry, it's not for long.  We'll pop up near the South Maitland Tidal Bore interpretive centre which is well worth a visit.  We'll then head to Truro to the Mi'Kmaq Heritage Centre to learn about our First Nation – what you learn here can and will impact how you look at our natural beauty and history.  Lunch will be at the Cove restaurant which is perfectly situated next to the Canso Causeway, the gateway to beautiful Cape Breton Island. We'll head up the highway to Cape Breton, were we'll be spending the evening exploring Baddeck and where we'll have dinner and spend the night.</p> 

<p>*Given your interests in the above we could most certainly extend touring in this area with an overnight stay in Truro for a more focused day of touring in Maitland before traveling on to Cape Breton.</p>
<img src="../../images/novascotia/seafoodtrail/seafoodtrail_4.jpg" class="img-rounded">

<h3>Day 4: Cabot Trail Tour, Mussels and Chowders</h3>

<p>Following breakfast, we'll begin the Cabot Trail, heading towards Ingonish.  There are so many places to pull over and walk or photograph...we don't mind!  We will stop for lunch in Neils Harbour at the Chowder House, one of BDT's owner, Bob, favourite places. We'll continue along the trail, stopping for strolls or a photograph or two.  We'll end our evening in Mabou at the Red Shoe Pub (we highly recommend the mussels while you stomp your feet to traditional music)!  We'll travel through the Margaree Valley back to Baddeck which is where we'll spend the evening.</p>
<img src="../../images/novascotia/seafoodtrail/seafoodtrail_5.jpg" class="img-rounded">
<h3>Day 5: Warm Shores and Warm Hospitality</h3>

<p>Following breakfast in Baddeck we'll leave Cape Breton Island and head to the town of Pictou. Take your time to explore this town – shop, eat, learn and relax!  We suggest eating at Harbour House Ale and Spirits but the choice is yours – seafood is abound.  If you don't want to eat a lobster, you can adopt one instead at the Northumberland Fisheries Museum, the only lobster hatchery in Canada!  After lunch, we'll follow the delightful "Sunrise Trail" along the coastline to the quaint village of Tatamagouche.  Shop, explore museums, enjoy Tata Brew, hop on the road train, indulge in a treat or walk/cycle one of the trails. We'll head to Port Phillip to a local restaurant, the Sand Piper, for supper – local, home cooked and casual dining.  Feeling full and satisfied, we'll go back to Halifax via the scenic Wentworth Valley. If you and yours are not too travel weary perhaps one final stop at Peggy's Cove for a picture opportunity and a lasting reminder of your adventures in Nova Scotia!</p>
<img src="../../images/novascotia/seafoodtrail/seafoodtrail_6.jpg" class="img-rounded">
