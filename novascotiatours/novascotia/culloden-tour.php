							<p>Continuing eastwards, we’ll pass through the village of McArra’s Brook.  Settled in 1791, it was first called Moidart after its namesake place in Scotland until a British soldier, James McCara, changed the name to McCara’s Brook.  As we pass through, take some time to wonder what it was like for the settlers who arrived here… and look out to the Strait to see if you can catch a glimpse of the famous “Ghost Ship of the Northumberland Strait”!</p>
							 
