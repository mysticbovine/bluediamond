							<h2>DAY TWO – The Cabot Trail</h2>
							<p>Today, we'll spend most of the day traveling the world famous Cabot Trail. The Cabot Trail is one of the best road trips in the world and winds through stunning
							 landscapes, along the Margaree River and through the spectacular rugged highlands of the Cape Breton Highlands National Park.  The trail was named after Italian
							 explorer John Cabot (Giovanni Caboto) who reached our shores in 1497, sailing on a mission for King Henry VII of England. The name also celebrates the meeting of
							 Cabot and the Mi'kmaq people, the original inhabitants of Cape Breton Island.</p>

							<p>The Cabot Trail, completed in 1932, joined previously isolated fishing villages along approximately, a 300 km loop. Today, the Cabot Trail connects eight major
							 communities with intriguing histories, including Acadian, Irish and Scottish settlements.</p> 
																			  
							<p>Following our road trip, we'll visit the Alexander Graham Bell Museum. This national historic site communicates the story of Alexander Graham Bell's wide ranging
							 interests and inventive work, much of it undertaken in Baddeck. The site boasts about 10 hectares overlooking Baddeck Bay, on Bras d'Or Lake, and Beinn Bhreagh,
							 Bell's summer home, where Bell pursued much of his scientific work. The museum commemorates and interprets the work of Alexander Graham Bell and his associates
							 and also functions as a centre for the study of Bell's scientific and humanitarian work, as illustrated by the artifacts and documents preserved there.
							 We'll spend the evening in Baddeck.</p>
							 
							 <img src="../../images/capebreton/louisbourg.jpg" alt="Stone walls and fortications on the water" class="img-thumbnail">
							<h2>DAY THREE - Louisbourg.</h2> 
							<p>Following a leisurely breakfast, we'll depart for Louisburg to explore a National Historic site.</p> 

							<p>Fortress Louisbourg is an intimate part of the fabric of Nova Scotian history, involving French, English, American, and Mi'kmaq peoples.
							 It has been called the 'Gibraltar of North America' and was so expensive to build, that Louis the Fourteenth, of France "expected to see its walls from Paris"!
							 Originally, settled in 1713 by a French expedition relocating from Placentia, Newfoundland, by 1740, Louisbourgs population grew to reach 2500 civilians and 700
							 garrison personnel. The population was comprised of Bretons, Normans, Basques and other French, along with Germans, Swiss, Irish and Africans, and local Mi'kmaq
							 – quite a medley of cultures!</p>

							 <p>In 1928, Fortress Louisbourg was proclaimed a National Historic Site and in 1961, the Government of Canada began to reconstruct one-fifth of Louisbourg, using the
							 original blueprints to recreate identical building units. Today, it's the largest fortress re-construction site in North America, with over 25 open buildings for you
							 to explore.</p>

							 <p>During your visit, you will find dozens of costumed animators who portray the residents of 1744. Period costumes and exhibits line the streets and along the busy
							 waterfront. See how the wealthy elite lived, contrasting to the very different life experienced by the poor. We will provide you with a private, one-hour, guided
							 tour of this Fortress, then you will have ample time to explore the unique site on your own.</p>

							 <p>We'll be dining in Louisbourg, so bring your hunger to one of the 18th-century restaurants. The food is fresh and plentiful, the recipes are from the 18th-century
							 and the atmosphere is New France in the New World! Hotel De la Marine is a busy waterfront tavern and where mariners, soldiers and fishermen would have supped, with 
							 only a spoon on the table. Grandchamps is another lively waterfront cabaret or perhaps you would prefer L'Epee Royale, which recreates the atmosphere and menu of a 
							 comfortable inn, an upscale eatery where the tavern crowd rarely ventured…and a place where you can show your proper breeding and eat with a full set of cutlery!</p>

							 <p>We'll return to Baddeck for the evening.</p>
							 
							<h2>DAY FOUR – Halifax Bound</h2>

							<p>Leaving Baddeck, we'll meander our way through spectacular scenery to Bras d' Or Look-off which will allow for amazing views of the Bras d'Or lakes.  We'll continue
							 on to St. Peter's, where we'll cross St Peter's Canal - an 800m canal linking the Atlantic Ocean with the Bras d'Or Lakes. Work started on this canal in 1854 and
							 was completed in 1869. The canal also boasts the only functioning lock system in Nova Scotia.</p>

							</p>Once again, we'll cross the Canso Causeway back onto the mainland and enjoy a scenic drive to the town of Pictou. Located on the beautiful Northumberland Shore of
							 Nova Scotia, Pictou is known as the "Birthplace of New Scotland" as it was here, the first wave of Scottish immigrants landed in 1773. Explore the world class
