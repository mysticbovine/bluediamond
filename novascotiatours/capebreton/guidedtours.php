							<p>Cape Breton Island (French: ile du Cap-Breton - formerly Ile Royale, Scottish Gaelic: Ceap Breatainn or Eilean Cheap Bhreatainn, Mikmaq: Unamakika, simply: Cape Breton) is an island on the Atlantic coast of North America. The name probably derives from Cap Breton near Bayonne, or to the name Breton, the Anglicized version of the French historical region, Bretagne.</p>

<p>Cape Breton Island is part of the province of Nova Scotia, Canada. Although physically separated from the Nova Scotia peninsula by the Strait of Canso, it is artificially connected to mainland Nova Scotia by the 1,385 m (4,544 ft) long rock-fill Canso Causeway. The island is located east-northeast of the mainland with its northern and western coasts fronting on the Gulf of Saint Lawrence; its western coast also forming the eastern limits of the Northumberland Strait. One of the world's larger salt water lakes, Bras d'Or ("Arm of Gold" in French), dominates the centre of the island.</p>

<p>The island contains five reserves of the Mi'kmaq Nation, these being: Eskasoni, Membertou, Wagmatcook, Waycobah, and Potlotek/Chapel Island. Eskasoni is the largest in both population and land area.</p>

<p>The turn of the 20th century saw Cape Breton Island at the forefront of scientific achievement with the now-famous activities launched by inventors Alexander Graham Bell and Guglielmo Marconi.</p>

