							<h3>Day 1 &ndash; All about the Tidal Bore</h3>
              <p>The Bay of Fundy is known for having the highest tides in the world, so what better way to start your vacation than becoming acquainted with them. We'll begin our day by visiting Burntcoat Head, home of the world's highest tides, where we can explore the ocean floor&hellip;.prepare to get muddy! A short drive will take us to the historic community of Maitland, perhaps stopping for refreshments at <em>Frieze and Roys, </em>Canada's oldest general store. Now we've walked on the floor, it's time to ride the bore! Tidal bore rafting is a unique activity offered in this area and one we highly recommend, it's unlike any other rafting experience. Jump in a boat and gently cruise up the river, enjoy the gorgeous scenery, perhaps spot some eagles or pop out and play on the mudflats. As the bore approaches, hold on for the ride of your life! Trained guides will lead the boat on top of the bore, play in raging rapids and the odd whirlpool or two. It truly is a rafting ride you will never forget! As the bore ends, it's time to bring out your inner child and go mud sliding. Yes, you read correctly, mud sliding. The beautiful mud makes a natural, perfect slide into the river! If you wear a white T-shirt, you can create your own custom souvenir of the experience &ndash; a dyed mud shirt! This exciting adventure is sure to exhilarate your senses!</p>
              <p>We'll return to Halifax where you can spend the evening in your hotel trying to remove mud, prior to dining in one of Halifax's superb restaurants.</p>
