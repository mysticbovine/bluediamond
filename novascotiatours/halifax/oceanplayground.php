              <h3>Day 2 &ndash; Flying High and Laying Low</h3>
              <p>Now you're well acquainted with the mud and water, it's time to take in a Birds-eye-view of the Bay. We'll head to Debert Flight Centre, where a private, chartered flight will offer breath taking views of the Bay of Fundy. This truly is an awe inspiring experience not to be missed &ndash; make sure your camera batteries are charged!</p>
              <p>Coming down from our high, we're going to go low, below ground. Debert is also home to the Diefenbunker &ndash; a decommissioned Cold War bunker! Take some time to explore this declassified nuclear facility and learn more about Canada's role in the Cold War. If it's more excitement you crave, perhaps participate in laser tag or one of their "escape" rooms.</p>
              <p>Leaving Debert, we'll take a scenic drive through the Wentworth Valley then follow the Sunrise Trail to the quaint village of Tatamagouche. Take some time to explore this gorgeous little town, it has a lot to offer. Taste some "Tata Brew", savour the delights of quaint cafes, hop on the road train or cycle on the trails. We can't come to this area without spending some time on a beach &ndash; the clear, ocean waters here are the warmest north of the Carolinas! Relax, warmly float in the ocean and enjoy the sand between your toes.</p>
              <p>We'll return to Halifax via a scenic route, which if time allows, we'll visit a maple sugar farm so you may indulge your sweet tooth. We'll be back in the city in time for your evening meal.</p>
              <p><img src="../../images/halifax/oceanplayground02.jpg" alt="cid:image003.png@01D4F905.9DCA4AA0" width="624" height="165" border="0" /></p>
              <h3>Day 3 &ndash; Sailing, Surfing and Saving</h3>
              <p>Following a hearty breakfast, we'll head towards the eastern shore of Nova Scotia to the community of Fisherman's Cove. Explore this quaint little fishing village, prior to boarding a boat and sailing to McNabs Island &ndash; "Halifax's Best Kept Secret". McNabs Island is about 5 km long, 1.5 km wide and offers a combination of historical and natural features. Take some time to hike (or stroll) this significant wee island, either with or without your own, personal tour guide.</p>
              <p>When we return to the mainland, we'll continue to follow the coastline to the community of Lawrencetown. Here, you'll squeeze yourself into a wetsuit and with your own trainer, will learn how to surf the waters of Nova Scotia! With superb scenery as a backdrop, perfect waves, it is a super place to surf, or for those with less energy, float in awe!</p>
              <p>While we're in this area, we'll visit Hope for Wildlife. This rehabilitation centre rescues, rehabilitates, and releases over 4500 injured and orphaned animals or birds back into the wild, per year. It has also become a very popular television series. This facility is truly worth the visit.</p>
              <p>We'll return to Halifax in time for dinner. Take some time to explore the city on foot, stroll along the boardwalk and enjoy the excellent nightlife this historic city offers.</p>
              <p><img src="../../images/halifax/oceanplayground03.jpg" alt="cid:image004.png@01D4F905.9DCA4AA0" width="624" height="165" border="0" /></p>
              <h3>Day 4 &ndash; Hounding and Hiking</h3>
              <p>Nova Scotia is well known for rock hounding &ndash; searching for geological gems. Our coastline offers the opportunity to stumble across ancient fossils or gems such as amethyst. Today, we're going to head towards the Annapolis Valley, stopping first at Blue Beach. We'll visit the Blue Beach Fossil Museum where you can see the "world's oldest collection of fossil footprints, and the rare bones of the creatures that left them behind". A beautiful stroll will lead us onto the shore where, with your new found knowledge, you can search for fossils and gem stones. This beach is quite unique as it is a shale beach. Take your shoes off and let your feet absorb history, all while keeping your eyes open for a fossil!</p>
              <p>We'll meander through the Valley, perhaps taking in some Acadian history or strolling on ancient dykelands, before we head up "the mountain". We'll stop for breathtaking photos at the "Look Off" then continue to Blomidon beach and finally up to Scots Bay and Cape Split.</p>
              <p>Cape Split is an &lsquo;iconic provincial coastal landmark overlooking the Bay of Fundy' and offers an amazing trail (approx. 12km round trip). Allowing almost 3hrs for this hike, we're sure you'll enjoy every moment. If the hike is timed correctly, you can hear (and feel) the power of the rushing water entering the Bay!</p>
              <p>When you return exhilarated and slightly exhausted from your hike, we'll have the choice of oceanside lobster dining in Halls Harbour or visiting Gasperau Vineyards to relax and unwind before heading back to the city.</p>
              <p><img src="../../images/halifax/oceanplayground04.jpg" alt="cid:image005.png@01D4F905.9DCA4AA0" width="624" height="165" border="0" /></p>
              <h3>Day 5 &ndash; Paddling and Playing</h3>
              <p>An early morning start will take us to Peggy’s Cove where we’ll breakfast on the rocks.  Following which, we’ll follow the coastline and explore the fishing villages we pass through, until we reach our destination, “The Ovens”. This natural park offers a superb trail system which is a sight and sound sensation!  The natural sea caves amplify the crashing waves entering them, a sound so unique, you will never forget.  We’ll spend some time in Lunenburg, perhaps boarding (or even sailing on) the iconic Bluenose II, prior to heading to the community of Prospect for the afternoon.  This is a gorgeous location for an afternoon paddle on the water!  Relax, paddle and enjoy as we kayak around the coastline of this community.</p>
              <p>We’ll return to Halifax in time for dinner and perhaps arrange for an outing to the theatre or a concert, the choice is yours.</p>

