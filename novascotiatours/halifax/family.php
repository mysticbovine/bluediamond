						<p>After the museum, we'll drive along the shoreline of Halifax
						Harbour, which is the second largest natural harbour in the world. We'll pass
						by numerous historical sites, Canada's largest navy base, a large ship yard,
						and pass under two bridges where we'll pause to hear the story of one of the
						greatest man-made disasters of all time; the Halifax Explosion of 1917. Then
						we're off to Seaview Park which is located on the inner harbour known as
						Bedford Basin. This is where the convoys of WWI &amp;WWII began their journey
						across the Atlantic to Europe. Seaview Park has a great playground for the kids
						to enjoy. </p>

