						<p>During this excursion you'll visit<strong> five museums</strong> that through their collections, interpretations  and exhibits, will give you an <strong>overview and an appreciation of Halifax's  heritage</strong>. As we drive  from one museum to the next you will also have an opportunity to see much of  historic Halifax and Dartmouth. </p>
        <h2>Maritime  Museum of the Atlantic</h2>
        <p> The first stop is the <em>Maritime  Museum of the Atlantic</em>, the largest site in Nova Scotia that collects  and interprets the various elements of our marine history. During this visit you will learn about the  'Age of Steamships' and see various examples of local boats, vessels of both  the Royal Canadian and Merchant Navies and WWII convoys. You'll also learn about the 'Battle of the  Atlantic', the Halifax Explosion of December, 1917, and Nova Scotia's role in  the Titanic disaster. This stop will  last approximately 60 minutes. </p>
        <h2>Halifax Citadel Fortress</h2>
        <p> Next, we're off to visit the Halifax  Citadel. The present Citadel is the fourth in a series of forts to occupy the  hill overlooking Halifax Harbour. It is  an excellent example of a fortification circa the 19th century, and is complete  with defensive ditch, ramparts, musketry gallery, powder magazine and signal  masts. Your driver guide will introduce  you to the fort, point out the location of the museum, gift shop and caf&eacute;,  powder magazine, exhibits of interest, washrooms, stairways, etc. You may choose to join one of the guided  tours given by Citadel staff (dressed in period costume), or you may opt to  tour on your own. Our stop here will  last for approximately 60 minutes. </p>
        <h2>Admiralty  House Museum</h2>
        <p> Halifax was one of four principal overseas  naval stations of the British Empire and, most importantly, was a Royal Navy  dockyard. Our city was/is called &ldquo;The  Warden of the North&ldquo; because of it's commanding presence on the North  Atlantic. High on a hill overlooking the  Dockyard is the stately Georgian mansion known as Admiralty House, once the  official summer residence of the Admiral of the North American Station. It is now the Maritime Command Museum and our  next stop. Here we'll find more than 30  rooms of displays including model ships, medals, badges, ship's bells and other  memorabilia, as well as armaments and accouterments associated with navel life.  Our visit will last for approximately 40 minutes. </p>
        <h2>Shearwater  Aviation Museum</h2>
        <p> Next, we'll drive across the second of  Halifax's harbour bridges, the <strong>A. Murray MacKay </strong><strong>(circa 1971</strong>) which will give us a great  view of the inner harbour known as <strong>Bedford Basin</strong>. We'll also be able to see the anchorage site  from where the convoys of WWI and WWII departed. This is also the area where the December, <strong>1917  Halifax Explosion</strong> occurred.  Our drive/tour will take us along the harbour on the Dartmouth side and  will afford us great views of both Halifax and the harbour itself. </p>
        <p>Halifax harbour is the second largest  natural harbour in the world after that of Sydney, Australia.  The story of our next stop involves the <strong>United  States Navy and it's Air Station circa 1918-1919</strong>. Starting out  from Bedford Basin, convoys of ships were successfully supplying Great Britain  by 1917. The enterprise was so  successful the German Navy had to change its focus and deploy large, sea-going  submarines (complete with mounted, six-inch guns) off the coast of Nova Scotia. </p>
        <p> As neither Canada nor Britain had suitable  aircraft to counter this threat, the United States agreed to extend its air  patrols to the coasts of Nova Scotia.  They established two air stations, one at Halifax (called Shearwater)  and one at Sydney on the island of Cape Breton.<strong><br clear="all">
        </strong>        </p>
        <p><strong>Lieutenant  R.E. Byrd (later Admiral Byrd of South Pole fame)</strong> was tasked with the establishment of  these air stations. We'll visit the  Shearwater Aviation Museum (circa 1978) which has over 6000 artifacts. The museum has eleven derelict/retired  aircraft which represent the various types of aircraft flown from the  base. We will spend approximately 60  minutes at the museum. </p>
        <h2>Nova Scotia  Museum of Natural History </h2>
        <p>          Returning to Halifax, we'll stop at the  Nova Scotia Museum of Natural History near Citadel Hill. Look for the giant Spring Peeper (a type of  frog) attached to the side of the building.  The museum has exhibits on man and his environment in Nova Scotia. T his  is the place to learn about<strong> fossils, dinosaurs, whales, birds and</strong><strong> more</strong>. They have live <strong>snakes,  frogs, insects, and butterflies</strong> in their Nature Center. During our visit, we'll stop and say hello to  the museum mascot, <strong>Gus, an 84+ year old tortoise</strong>. Our visit will last for approximately 60  minutes. </p>
        <p><strong>At your  convenience, we will make a stop for lunch</strong>. <strong>All  the museums have washrooms.</strong></p>
        <p>After our stop at the Nova Scotia Museum  of Natural History, we'll make our return to the cruise ship dock where we'll  find our next museum, <strong>Pier 21 - Canada's Immigration Museum</strong>,  sometimes referred to as Canada's Ellis Island.  Pier 21 has the goal of telling the story of all immigration to Canada  along with the mandate to tell the story of nation-building by highlighting our  earliest beginnings and immigration from 1867 to the present day. While not an official part of your excursion  (unless you want it to be), you should have time to <strong>explore  Pier 21 on your own as your ship is just a few feet away. </strong></p>
        <p>As with all our excursions, both the sites  and itinerary are not fixed. We can and  will modify any excursion to suit what you, our guest, wishes to see,  experience and do. All you need to do is  ask.</p>
