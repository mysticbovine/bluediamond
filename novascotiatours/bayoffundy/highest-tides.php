						<h2>Maitland</h2>
						<p>Following the shoreline, we'll travel through the historic village of Maitland, Nova Scotia's first, municipal, Heritage Conservation District.  During the Age of Sail, Maitland was a large, bustling community and home to numerous ship yards, businesses and sea Captains.  Today, the shipyards and businesses are long gone, but many of the impressive homes remain, one of which is a provincial museum.  The Lawrence House museum was home of W.D. Lawrence, a shipbuilder who, despite much public ridicule, built the largest, wooden-hulled, fully rigged ship in the world, successfully launching it in 1874.  As we drive through this community, imagine the streets lined with people, bustling ship yards on the shoreline and an air of excitement and anticipation in preparation of the great launch.</p>

