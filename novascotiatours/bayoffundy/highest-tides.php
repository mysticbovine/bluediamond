						<h2>Walton</h2>
						<p>From one lighthouse to another of a very different design.  The Walton lighthouse is the oldest lighthouse in Hants County (built 1873) and is perched on top of cliffs overlooking the Bay of Fundy. This lovely little lighthouse once guided ships loaded with gypsum, pulpwood and barite into the harbour, which in the 1950's was one of the busiest ports in Nova Scotia.  Today, decommissioned, it still watches over the now peaceful harbour and offers visitors a glimpse into its busy past.</p>

