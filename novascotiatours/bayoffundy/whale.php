							<p>This three day tour takes you around the southern tip of Nova Scotia, from lush valleys to rugged coastlines.  You'll see sights that will stay with you for years to come.</p>
							<img src="../../images/fundy/blomingdon.jpg" class="img-thumbnail" alt="North Grand Pre">
							<h3>Annapolis Valley</h3>
							<p>From Halifax, we'll travel to the Annapolis Valley to begin our journey in Grande Pr&eacute;  - a UNESCO world heritage site and an important part of our Acadian history. The interpretive centre and beautiful gardens will educate and captivate you.  Following this tour, we'll stop for lunch at a "Gastropub" which is sure to satisfy your taste buds!</p>
							<p>This area has abundant wineries and spectacular 'look-offs' so a stop at one (or two) is an absolute must!</p>
							
							<img src="../../images/fundy/fortanne.jpg" class="img-thumbnail" alt="Fort Anne">
							<h3>Annapolis Royal</h3>							
							<p>Travelling along the scenic back roads of Nova Scotia, we'll head to Annapolis Royal - one of the oldest settlements in Nova Scotia.  Here, we'll tour two National Historic sites:  Port-Royal and Fort Anne.</p>
							<p>Port-Royal is a National Historic Site of Canada and features a reconstruction of an early 17th century settlement.  This "hands-on" tour will make you wonder how settlers survived this harsh climate (perhaps by creating the 'Order of Good Cheer'?) and help you appreciate all we have today.</p>
							<p>Fort Anne is Canada's oldest National Historic Site, originally built by the Scottish in 1629 with subsequent forts built by the French, and has played an important role in Nova Scotia's history.  Take a tour back in time, enjoy spectacular views and visit the oldest English graveyard in Nova Scotia. Fort Anne National Historic Site is a Bay of Fundy Recommended Experience.</p>
							<p>You will also have some free time to explore this magnificent little town with so much history and quaint shops at your fingertips.  Take some time to "smell the roses" at Annapolis Royal Heritage Gardens (2015 Canadian Garden Tourism Award - "Garden of the Year"), visit the Sinclair Inn or O'Dell House museums or simply sit back and enjoy the peaceful panoramas.</p>
							<p>Dinner will be your choice of any one of the superb local restaurants.  Following dinner, we can arrange a unique, candlelight tour of this historic town - an experience not to be missed!  After an exhausting day, collapse in a sumptuous room in one of the superb historic inns.</p>
							
							<img src="../../images/fundy/brierisland.jpg" class="img-thumbnail" alt="Looking down the pier on Brier Island">
							<h3>Brier Island</h3>							
							<p>From Annapolis Royal, we'll head towards the most westerly point of Nova Scotia, Brier Island.  This scenic drive also includes two short ferry rides - have your cameras ready!  Following a brief tour of the island and check-in at a rustic lodge, you'll head out to sea for what is guaranteed to be an exciting whale watching adventure!  Dinner and a relaxing evening will be at the lodge.</p>
							
							<img src="../../images/fundy/kejimkujik.jpg" class="img-thumbnail" alt="Still Brook in Kejimkujik Park">
							<h3>Liverpool & area</h3>							
							<p>As we leave Brier Island and Digby Neck, you'll have the option of a short hike (2.5km) to see 'Balancing Rock' - one of the most photographed images on Long Island.  Back on the mainland, we'll have a quick stop in Annapolis Royal to collect a "picnic lunch" which we'll have at Kejimkujik National Park and Historic Site.  We'll spend some time exploring this spectacular park and all it has to offer before we head to the shores on the opposite side of the Province.</p>
							<p>On arrival in Liverpool, we'll visit one of the most unique museums in the province, one which is sure to captivate even the most unlikely museum goer!  Following this tour, we'll head to one of Nova Scotia's popular beach resorts where you can relax and unwind - or if the mood takes you, participate in a surfing lesson!</p>
							
							<img src="../../images/fundy/lunnenburg.jpg" class="img-thumbnail" alt="Lunenburg Harbour">
							<h3>South Shore</h3>							
							<p>Following breakfast, we'll head along the shoreline to "The Ovens Natural Park" to see the 'sea caves' - not only a visual sight but a sound experience! An easy, self-guided, one hour tour will ignite all your senses!</p>
							<p>A short drive will take us to Lunenburg - A UNESCO world heritage site.  Here, enjoy strolling along streets lined with artisan stores, art galleries and museums.  Walk along the board walk and tour the Bluenose II, take in spectacular vistas and enjoy lunch at one of the numerous eateries; the hearty Nova Scotian seafood chowder you may have heard about can be found here!</p>
							<p>Leaving Lunenburg, we'll visit the beautiful seaside communities of Mahone Bay and Chester, which offer amazing photo opportunities, shopping and delectable delights. </p>
							<p>We'll return to Halifax in time for your evening meal.</p>
					
							
							
