							<h3>Hall's Harbour</h3>
							<p>Hall's Harbour is named after Samuel Hall, an American privateer who, during the American Revolution, used the cove to raid settlements in the Annapolis Valley. Now it's home to a Bay of Fundy fishing fleet and one of the largest lobster pounds in the world. Its unique size makes it an ideal place to fully witness the startling wonder of the Fundy tides.  Be sure to capture a picture of what the harbour looks like now and compare it with the view you’ll see upon our return.</p>
							<p>From Hall's Harbour, we'll drive over the North Mountain Ridge to the Blomidon ‘Look-Off' where, on a clear day, you can see the Bay of Fundy, Grand Pré, Wolfville and the extensive dykelands built by the Acadians.  It is perhaps, one of the most scenic vistas in Nova Scotia.</p>
							<p>Following our drive "down the mountain", we'll visit Grand Pré National Historic site to learn the history of the 1755 Acadian deportation. This beautiful site and interpretive centre recalls the sad story of the deportation and illustrates the history of a courageous people whose culture and actions continue to enrich Canada. Our visit will take approximately one hour. We may also visit two other sites associated with this dark time in Nova Scotia's history.</p>
							<p>A short drive from Grand Pré will take us to Wolfville, which at one time, was a prosperous agricultural community. It is a University town so there are numerous, delicious eateries to choose from for lunch. Take a stroll around Acadia campus (just named to the 2016 list of "The Most Beautiful Campuses in Canada") or, on the edge of the town, you'll find one of the French dykes which you can walk along and enjoy some spectacular scenery. Additional sites and dining options are also available.</p>
							
							
