							<img src="../../images/btdcollection/Goldcoast.jpg" alt="View of Halifax's water front boardwalk. People sit and eat in outdoor patios." class="img-thumbnail">
							<h2>Day One: Welcome to Halifax</h2>

							<p>Following your arrival at Halifax's award-winning Stanfield International Airport,
							 you'll be transported to your downtown hotel and given a brief orientation of the surrounding your area
							 - nearby restaurants, shops, and attractions, then the balance of the day is at your leisure.
							 We will provide recommendations for dinner, with the choice of restaurants you have within the proximity of your hotel.</p>

							<img src="../../images/fundy/valley.jpg" alt="A view of a fertile valley from a few hundred meters up." class="img-thumbnail">
							<h2>Day Two: The Annapolis Valley</h2>

							<p>Following breakfast, you'll enjoy a brief overview of historic Halifax before leaving the city to tour the Annapolis Valley, "land of Evangeline".
							 (Evangeline was the heroine of Henry Wadsworth Longfellow's Poem Evangeline, A Tale of Acadie).<p>

							<p>Our first stop will be at the Dill Farm in Windsor, Nova Scotia - famous not only for 1600 lb Pumpkins and pumpkin yachts,
							 but as a sacred site in hockey lore – it’s reportedly the site of the first ever recorded hockey game. Then it's a coffee stop at Just Us Coffee Roasters,
							 Canada's first Fair Trade coffee roaster which is near both Wolfville and our next destination, Grand Pr&eacute;.</p>

							<p>A short drive will take us to Grand Pr&eacute; National Historic Site (also a UNESCO World Heritage Site).  Grand Pre tells the story the Grand D&eacute;rangement – the expulsion of the Acadians in 1755.
							 Their story was immortalized by Longfellow and the site features a film telling their tale, a replica of the Church of St. Charles de Mines, and a statue of Evangeline herself.
							 As you walk around the statue, the young Evangeline morphs into the older Evangeline at the end of her search for Gabriel.
							 We will be at this national historic site for approximately one hour.</p>

							<p>The university town of Wolfville is the next stop, home to Acadia University and, at one time, an important, prosperous agricultural community.
							 As it is a university town, there are numerous eateries to choose from for lunch. On the edge of the town we'll find one of the French dykes that you can walk and begin
							 to understand how the Acadians created such rich and valuable farmland on the edge of the Bay of Fundy.<p>

							<p>The Annapolis Valley is noted world-wide for its fruit trees, particularly apple trees although there are also pear, plum, grape, cherry, and assorted berries and vegetables
							 farmed here. Basically, the Valley produces everything from apples to zucchini!</p> 

							<p>We'll drive past apple orchards and dyked farmland as we climb 700 feet above the Valley floor to the Lookoff at Blomidon.
							 On a clear day, you can see four counties, the Minas Basin, the spires of Wolfville and Kentville, Acadia University, and the farms and wineries in the Valley.</p>

							<p>A short drive from the Lookoff will take us to Hall's Harbour. This village is one of the best places to view the rise and fall of the tides. It has a colourful history
							 as a pirate's lair and a painter's paradise. The tides in this tiny harbour rise/fall an inch per minute and you can easily see this during the time you are here.
							 The village is home to one of the largest lobster pounds in Canada, and if you wish, you can enjoy a lobster dinner al fresco on the wharf before we return to Halifax.
							 We return to Halifax and the balance of the day is at your leisure - dinner is on your own.</p>

							<img src="../../images/fundy/fundy-mud.jpg" alt="The tide is out showing the red mud of the Bay of Fundy. In the distance the mud continues to an rocky out-crop." class="img-thumbnail">
							<h2>Day Three: The Bay of Fundy</h2>

							<p>'F' is for Fundy, the Bay of Fundy that is, where you'll find the highest tides in the world!<p>

							<p>Following breakfast, we'll make our way to the Glooscap Trail and the Maitland area of Nova Scotia, on the shores of Cobequid Bay, Minas Basin, and the Shubenacadie River.</p>

							<p>According to Mi'kmaq legend, Glooscap was a God-like man who lived in the high bluffs of Cape Blomidon overlooking the Bay of Fundy.
							 He was created by the Great Spirit and brought to life when a lightning bolt struck the sand. Glooscap brought wisdom and knowledge to the Mi'kmaq people and created
							 the Bay of Fundy by smashing an evil beaver's dam. (It's interesting to note that the name Glooscap means joker.)</p>

							<p>You will experience unparalleled vistas of stunning, natural, scenic beauty not found elsewhere in Nova Scotia.
							 We'll visit the Fundy Tidal Interpretive Center and Tidal Observation Deck where you can learn about the highest tides in the world and the unique Tidal Bore.
							 From here, you will be able to stroll the easy-to-walk nature trail and learn about the impact of Fundy's tides along the Shubenacadie's river flats.</p>

							<p>At Maitland's Dawson Dowell Park, you'll be able to walk out on a tidal look-off pier - this is also the area where the Shubenacadie Canal, designed to connect the Bay of Fundy to Halifax Harbour ends.</p>

							<p>At Anthony Provincial Park, depending on the tide, you may be able to walk a very long distance to reach the salt-water of Cobequid Bay -
							 the beach is red sand and there interpretative display panels telling the story of this area on the Bay of Fundy.</p>

							<p>The highest tides in the world were recorded at Burntcoat Head in the Minas Basin. Nova Scotia has the greatest average tide at 47.5 feet and an extreme range of 53.6 feet.
							 Here is your opportunity to walk on the ocean floor which, at some point, will be covered with 47 to 55 feet of water!</p>

							<p>The next part of this truly amazing excursion will take us to the Salmon River Tidal Bore Park near Truro. Again, if tide and time are cooperating, you may see the Tidal Bore 'up front and personal'
							 - it truly is an awe-inspiring sight!  A short video (approximately 10 minutes long) in the park's information booth will fully explain the Bay of Fundy's tides and the tidal bore.
							 It is both interesting and informative and you'll be glad you took time to view it.</p>

							<p>Our final stop on this outing will be at the Glooscap Heritage Centre near Salmon River Park. This is a unique opportunity to learn about Mi'kmaq heritage and the true story
							 and importance of Glooscap, which is all interwoven with the Bay of Fundy and its tides. We allow a full hour to explore this aspect of Nova Scotian culture and history.
							 Following our visit, we'll return to Halifax – the balance of the day is at your leisure and dinner is on your own.</p>

							<img src="../../images/fundy/flowerpots.jpg" alt="Tall rocky out-crops with trees on the top, people walk around them on the red mud of the Bay of Fundy." class="img-thumbnail">
							<h2>Day Four: The Flower Pots</h2>

							<p>Leaving Halifax, we’ll have a brief stop near Truro and if the time for the tidal bore is ideal, we'll stop at the Salmon River Tidal Bore Park, before skirting along the coastline of Minas Basin.
							 We’ll stop at the Cobequid Interpretation Centre which has pictures, videos, and interpretive panels explaining the geology, history and culture of West Colchester.
							 We’ll also stop at a World War II era observation tower which offers breathtaking views of the Bay of Fundy and the surrounding tide-sculpted, countryside.</p>

							<p>A short, scenic drive will take us to Five Islands Provincial Park. Here you'll stand on 300-foot sea cliffs (sculpted again by the Fundy tides).
							 Five Islands is named, logically, after five small islands - Moose, Diamond, Long, Egg, and Pinnacle that are located just off the coast.
							 The Mi'kmaq legend tells us the Five Islands were created when their God, Glooscap, threw the mud, sticks, and stones at the giant beaver who had dammed his medicine
							 garden in Advocate. Every summer, a charity event called "Not Since Moses", offers a 5K and 10K run from Long and Moose Islands, over the sea bed to the shore,
							 attracting runners not scared to get just a little muddy!</p>

							<p>No visit to the area would be complete without a visit to the Fundy Geological Museum in Parrsboro - this seaside community is an ideal lunch stop location.</p>

							<p>Following lunch, we'll visit Joggins UNESCO World Heritage Site for a guided tour of the area. Often described as the "Coal Age Galapagos",
							 this area is where Fundy's tides continually reveal 300-million-year-old-fossils. Following our visit, we'll tour 'westward' towards Nova Scotia's border
							 with New Brunswick (making a stop or two along the way to visit the world-famous flower pots' of Hopewell Cape.</p>

							<p>The Hopewell Rocks, also called the Flowerpot Rocks, or simply The Rocks, are formations caused by tidal erosion at The Hopewell Rocks Ocean Tidal Exploration Site.
							 They are located on the shores of the upper reaches of the Bay of Fundy at Hopewell Cape near Moncton. Due to the extreme tidal range of the Bay of Fundy, the bases 
							 of the formations are covered in water twice a day, however, it is possible to view the formations from ground level at low tide.</p>

							<p>The vast sediment planes in the basin of Fundy support a variety of biological productivity and the area is a birder's paradise as it is on the northern flyway for 
							a number of species. Various shorebirds are often seen flocking to nest and feed in the area. As the tides vary from day to day, the high tide can be as high as 16 
							metres (52 ft) giving Hopewell Rocks one of the highest average tides in the world. We will overnight at a nearby rustic motel to give us the opportunity to visit 
							'the pots' after closing hours with the objective of observing both the low and high tide</p>

							<img src="../../images/fundy/digby.jpg" alt="Wharf in Digby. An old cannon points towards the harbour where fishing vessels are docked." class="img-thumbnail">
							<h2>Day Five: Saint John, New Brunswick to Digby, Nova Scotia</h2>

							<p>Following an early breakfast, (and we might if the timing is correct, re-visit the Flower Pots), we'll continue our tour through Fundy National Park. The road 
							through Fundy takes you along the shore and through dense forests on our way to Saint John, the largest community on the Bay of Fundy. If time permits, we'll have a 
							brief tour of the Loyalist city with its historic downtown, charming farmer's market, large town square, and royalist references before we take the 3-hour ferry ride 
							across the Bay of Fundy - Saint John to Digby. Believe it or not, it’s like a nature cruise - whales and other wild marine life can be seen frolicking beside the boat.
							 We will dine and overnight in Digby.  (The buffet dinner at the motel has been rated on a scale of 1 to 10 (10 being best) at 12!)</p>

							<img src="../../images/fundy/annapolis.jpg" alt="Seaward view at Annapolis Royal. Old battlements with cannons pointed toward the water." class="img-thumbnail">
							<h2>Day Six:  Annapolis Royal</h2>

							<p>Following breakfast, we'll head for Annapolis Royal (the former capital of Nova Scotia), a community at the centre of the history of Nova Scotia.
							 We’ll be visiting two important historic sites - Fort Anne and the Habitation – taking you back to the time of early French and English settlement of the province.
							 Annapolis Royal has beautiful older homes dating back three centuries, art galleries, and an historic garden. With time permitting, we may also visit the Annapolis 
							 Basin Tidal Generating Experimental Plant that harnesses the power of Fundy's tides to provide electricity to the province. We’ll return to Halifax by evening.</p>
